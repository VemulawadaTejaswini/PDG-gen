import java.util.*;

public class Main{
  public static void main(String[] args){
    Scanner sc = new Scanner(System.in);
    int N = sc.nextInt();
    int ans = 1;
    while(0==0){
      if(ans*2<=N){
        ans*=2;
      }
      else{
        break;
      }
    }
    System.out.println(ans);
  }
}
