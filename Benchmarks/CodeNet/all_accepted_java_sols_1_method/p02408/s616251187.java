import java.util.Scanner;

public class Main {

	public static void main(String[] args) {

		// ?????????????????\????????????
		Scanner sc = new Scanner(System.in);

		// ??\?????°????????????????????°
		int input = sc.nextInt();

		// ???????????????
		int[][] card = new int[4][13];

		// ?????°????????????????????????????????°????????????
		String mark;
		int num;

		// ?????????????????°?????????
		int cnt;

		// ??????????????????????????????
		for (cnt = 0; cnt < input; cnt++) {

			mark = sc.next();
			num = sc.nextInt();

			switch (mark) {

			case "S":
				card[0][num - 1] = 1;
				break;

			case "H":
				card[1][num - 1] = 1;
				break;

			case "C":
				card[2][num - 1] = 1;
				break;

			case "D":
				card[3][num - 1] = 1;
				break;
			}

		}

		// ???????????????????????\????????????
		// ????¨?????????°???num???cnt???????????¨
		for (num = 0; num <= 3; num++) {

			for (cnt = 0; cnt <= 12; cnt++) {

				if (card[num][cnt] == 0) {

					switch (num) {

					case 0:
						System.out.println("S " + (cnt+1));
						break;

					case 1:
						System.out.println("H " + (cnt+1));
						break;

					case 2:
						System.out.println("C " + (cnt+1));
						break;

					case 3:
						System.out.println("D " + (cnt+1));
						break;
					}

				}

			}

		}
		sc.close();
	}

}