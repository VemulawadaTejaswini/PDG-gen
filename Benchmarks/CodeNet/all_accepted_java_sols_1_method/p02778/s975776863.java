import java.util.*;
public class Main {
	public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
    
        String name = sc.next();

        char[] work = new char[name.length()];

        for(int i = 0; i < name.length(); i++){
            work[i] = 'x';

            //切り出した文字列を一文字ずつ出力
            }
        System.out.println(work);
	}
}