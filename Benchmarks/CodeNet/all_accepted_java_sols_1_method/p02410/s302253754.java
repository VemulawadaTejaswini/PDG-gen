import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;

class Main {
	public static void main(String[] args) throws IOException{
		
		//??????????????????????????????????????°??¨?????°?????\???
		BufferedReader br =
				new BufferedReader(new InputStreamReader(System.in));
		
		String[] str = br.readLine().split(" ");
		int rowNum = Integer.parseInt(str[0]);
		int colNum = Integer.parseInt(str[1]);
		
		int[][] arrayA = new int[rowNum][colNum];			 //??????A?????????????´????????????????arrayA
		for(int i=0; i<rowNum; i++) {
			String[] arrayAStr = br.readLine().split(" ");
			for(int j=0; j<colNum; j++) {
				arrayA[i][j]=Integer.parseInt(arrayAStr[j]);
			}
		}
		
		int[] arrayB = new int[colNum];					//??????B?????????????´????????????????arrayB
		for(int i=0; i<colNum; i++) {
			arrayB[i] = Integer.parseInt(br.readLine());
		}
		
		for(int i=0; i<rowNum; i++) {
			int sum = 0;
			for(int j=0; j<colNum; j++) {
				sum = sum +arrayA[i][j]*arrayB[j];
			}
			//????????????
			System.out.println(sum);
		}

	}

}