import java.util.*;

public class Main {
    public static void main(String[] args) throws Exception {
        Scanner sc = new Scanner(System.in);
        int x = sc.nextInt();
        int y = sc.nextInt();
        if (x == y) {
            System.out.println("Yes");
        } else {
            System.out.println("No");
        }
    }
}
