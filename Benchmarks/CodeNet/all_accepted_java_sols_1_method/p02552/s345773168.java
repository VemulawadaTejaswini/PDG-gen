import java.util.*;
public class Main {
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        // 整数の入力
        int a = sc.nextInt();
        if (a == 0) {
            System.out.print(1);
        }
        if (a == 1) {
            System.out.print(0);
        }
    }
}