import java.util.*;

public class Main {
    public static void main(String[] args) throws Exception {
        Scanner sc = new Scanner(System.in);
        int h = sc.nextInt();
        sc.nextLine();
        while(0 != h--){
            String str = sc.nextLine();
            System.out.println(str);
            System.out.println(str);
        }
    }
}