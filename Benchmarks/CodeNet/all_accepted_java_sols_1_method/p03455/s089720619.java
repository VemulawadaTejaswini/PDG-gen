import java.util.Scanner;
public class Main{
  public static void main(String args[]) {
    Scanner s = new Scanner(System.in);
    if((s.nextInt() & 1) == 0){
      System.out.println("Even");
    } else if((s.nextInt() & 1) == 0){
      System.out.println("Even");
    } else {
      System.out.println("Odd");
    }
  }
}
