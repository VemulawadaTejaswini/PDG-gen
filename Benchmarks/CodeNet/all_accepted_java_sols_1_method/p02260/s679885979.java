import java.util.*;

public class Main {
	public static void main(String[] args) {
		Scanner stdIn = new Scanner(System.in);

		int N = stdIn.nextInt();
		int[] numbers = new int[N];

		for (int i = 0; i < N; i++) {
			numbers[i] = stdIn.nextInt();
		}

		int sortCounter = 0;

		/*?????????????????¬???*/
		for (int i = 0; i < N; i++) {
			int min = numbers[i];
			int minIndex = i;

			/*??´?????????????????????????????§????°????????????????*/
			for (int j = i + 1; j < N; j++) {
				if (min > numbers[j]) {
					min = numbers[j];
					minIndex = j;
				}
			}

			/*??\???????????????????????????????????????*/
			if (minIndex != i) {
				/*??´????????????????????????????????¨????°?????????´????????\????????????*/
				int tmp = numbers[i];
				numbers[i] = numbers[minIndex];
				numbers[minIndex] = tmp;
				sortCounter++;
			}
		}

		/*????????????*/
		for (int i = 0; i < N - 1; i++) {
			System.out.print(numbers[i] + " ");
		}
		System.out.println(numbers[N - 1]);
		System.out.println(sortCounter);
	}
}