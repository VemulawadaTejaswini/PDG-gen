import java.util.Scanner;

public class Main {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);

		while (true) {
			// ??????????????°?????\??????
			// val:??\??????????????°?????????
			int val = sc.nextInt();

			if (val == 0) {
				break;
			}
			int[] score = new int[val];
			int sum = 0;
			int max = 0; 
			int min = 1001; 

			for (int i = 0; i < val; i++) {
				score[i] = sc.nextInt();

				// ??\???????????????????????§???????±??????????
				if (max < score[i]) {
					max = score[i];
				}
				// ??\??????????????????????°????????±??????????
				if (min > score[i]) {
					min = score[i];
				}
			}
			for (int j = 0; j < val; j++) {
				sum = sum + score[j];
			}
			int ave = (sum - max - min) / (val - 2);
			System.out.println(ave);

		}
	}
}