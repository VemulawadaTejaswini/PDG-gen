

import java.util.Scanner;

public class Main {

	public static void main(String[] args) {

		Scanner in = new Scanner(System.in);

		// ?????°??¨?????°?????????
		int line = in.nextInt();
		int column = in.nextInt();

		// ????????????????´??????????????????¨???
		int[][] matrix = new int[line + 1][column + 1];

		// ?????????????????????????´?
		for(int l = 0; l < line; l++){

			for(int c = 0; c < column; c++){

				matrix[l][c] = in.nextInt();

				matrix[l][column] += matrix[l][c];

			}
		}

		for(int c = 0; c < column; c++){

			for(int l = 0; l < line; l++){

				matrix[line][c] += matrix[l][c];

				matrix[line][column] += matrix[l][c];

			}

		}

		// ???????????????????????????
		for(int l = 0; l <= line; l++){

			for(int c = 0; c < column; c++){

				System.out.print(matrix[l][c] + " ");

			}

			System.out.println(matrix[l][column]);

		}

	}

}