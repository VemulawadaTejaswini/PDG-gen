import java.util.Scanner;


public class Main{

	public static void main(String[] args) {
		Scanner s = new Scanner(System.in);
		int cnt = Integer.parseInt(s.nextLine());
		int[][] input = new int[cnt+1][2];
		int[][] result = new int[cnt+1][cnt+1];
		for(int i=1 ; i<=cnt ; i++) {
			String[] tmp = s.nextLine().split(" ");
			input[i][0] = Integer.parseInt(tmp[0]);
			input[i][1] = Integer.parseInt(tmp[1]);
		}
		for(int i=0 ; i<result.length ; i++) {
			for(int j=0 ; j<result[0].length ; j++) {
				if(i==j)
					result[i][j] = 0;
				else
					result[i][j] = Integer.MAX_VALUE;
			}
		}
		//????§£?????§????????????????????????????§£??¬?????????????????£????????????2015/04/11
		//??°??????????????????????????????2???cnt?????§?±??????????
		for(int c=2 ; c<=cnt ; c++) {
			//??°?????????????§?????????????
			for(int i=1 ; i<=cnt-c+1 ; i++) {
				//????°??????????????±????
				int j = i + c -1;
				//?§??????????????????????1??\???????????????????????????????????????
				for(int k=i ; k<j ; k++) {
					result[i][j] = Math.min(result[i][j], result[i][k] + result[k+1][j] + input[i][0]*input[k][1]*input[j][1]);
				}
			}
		}
		System.out.println(result[1][cnt]);
	}

}