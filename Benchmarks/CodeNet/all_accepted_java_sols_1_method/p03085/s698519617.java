import java.util.*;
import java.math.*;
 
public class Main {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		// 整数の入
		String n = sc.next();

		if(n.equals("A")){
			System.out.println("T");
		}else 	if(n.equals("G")){
			System.out.println("C");

		}else 	if(n.equals("C")){
			System.out.println("G");

		}else 	if(n.equals("T")){
			System.out.println("A");

		}


 
	}
}
