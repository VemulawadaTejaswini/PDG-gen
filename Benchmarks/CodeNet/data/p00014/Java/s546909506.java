import java.util.Scanner;

public class Main {

	public static void main(String[] args) {
		// TODO ?????????????????????????????????????????????
		Scanner sc = new Scanner(System.in);

		while (sc.hasNext()) {
			int d = sc.nextInt();
			System.out.println(integ(d));
		}
	}

	public static long integ(int d) {
		int x = d;
		long ans = 0;
		while (x != 600) {
			ans += x*x * d;
			x = x + d;
		}
		return ans;
	}
}