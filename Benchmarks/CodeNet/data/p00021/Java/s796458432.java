class PLine{
	double a;
	double b;
	double c;
	
	/**
	 * 2???p1, p2???????????´??????????????????
	 * @param p1 1????????????
	 * @param p2 2????????????
	 */
	public PLine(PPoint p1, PPoint p2) {
		if(p1.y != p2.y) {
			this.a = 1;
			this.b = -(p1.x - p2.x)/(p1.y - p2.y);
			this.c = - b * p1.y - a * p1.x;
		}else {
			this.a = 0;
			this.b = 1;
			this.c = - p1.y;
		}
	}
	
	static boolean areParallel(PLine l1, PLine l2) {
		if(l1.a * l2.b == l1.b * l2.a) return true;
		else return false;
	}
	
	public String toString() {
		return a + "x+" + b + "y+" + c + "=0";
	}
}

