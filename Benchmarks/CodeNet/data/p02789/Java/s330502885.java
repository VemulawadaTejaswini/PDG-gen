import java.util.Scanner;

public class Solution{
  
  public static void main(String[] args){
    	Scanner scan = new Scanner(System.in);
    	int i = scan.nextInt();
    	int j = scan.nextInt();
    	if(i==j){
          	System.out.println("Yes");
        }else {
          	System.out.println("No");
        }
  }
}