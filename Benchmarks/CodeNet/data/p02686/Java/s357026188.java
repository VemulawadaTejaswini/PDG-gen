import java.io.*;
import java.util.*;

class Node {
  final int total, leftClose, rightOpen;

  Node(int total, int leftClose, int rightOpen) {
    this.total = total;
    this.leftClose = leftClose;
    this.rightOpen = rightOpen;
  }

  static Node createFromString(String s) {
    int leftClose = 0;
    int rightOpen = 0;
    for (char c : s.toCharArray()) {
      if (c == ')') {
        if (rightOpen > 0) {
          rightOpen--;
        } else {
          leftClose++;
        }
      } else {  // c == '('
        rightOpen++;
      }
    }
    int total = rightOpen - leftClose;
    return new Node(total, leftClose, rightOpen);
  }
}

class Solver {
  final int n;
  final String[] ss;

  Solver(int n, String[] ss) {
    this.n = n;
    this.ss = ss;
  }

  public boolean solve() {
    List<Node> list = new ArrayList<>();
    for (String s : ss) {
      list.add(Node.createFromString(s));
    }
    List<Node> totalPositive = new ArrayList<>();
    List<Node> totalZero = new ArrayList<>();
    List<Node> totalNegative = new ArrayList<>();
    for (Node node : list) {
      if (node.total > 0) {
        totalPositive.add(node);
      } else if (node.total < 0) {
        totalNegative.add(node);
      } else {
        totalZero.add(node);
      }
    }

    Collections.sort(totalPositive, new Comparator<Node>(){
      @Override
      public int compare(Node o1, Node o2) {
        return o1.leftClose - o2.leftClose;
      }
    });

    int totalSum = 0;
    for (Node node : totalPositive) {
      if (totalSum < node.leftClose) {
//        System.err.println("a");
        return false;
      }
      totalSum += node.total;
    }

    for (Node node  : totalZero) {
      if (totalSum < node.leftClose) {
//        System.err.println("b");
        return false;
      }
    }

    Collections.sort(totalNegative, new Comparator<Node>(){
      @Override
      public int compare(Node o1, Node o2) {
        return o1.rightOpen - o2.rightOpen;
      }
    });

    int totalSum2 = 0;
    for (Node node : totalNegative) {
      if (totalSum2 < node.rightOpen) {
//        System.err.println("c");
        return false;
      }
      totalSum2 -= node.total;
    }

    return totalSum == totalSum2;
  }
}

public class Main {
  private static void execute(ContestReader reader, ContestWriter out) {
    int n = reader.nextInt();
    String[] ss = reader.next(n);
    out.println(new Solver(n, ss).solve() ? "Yes" : "No");
  }
  
  public static void main(String[] args) {
    ContestReader reader = new ContestReader(System.in);
    ContestWriter out = new ContestWriter(System.out);
    execute(reader, out);
    out.flush();
  }
}

