public class Main {
    public static void main(String[] args) throws Exception {
        Field f = System.out.getClass().getDeclaredField("autoFlush");
        f.setAccessible(true);
        f.set(System.out, false);
        execute(System.in, System.out);
    }

    public static void execute(InputStream in, OutputStream out) {
        ExtendedScanner s = new ExtendedScanner(in);
        Out o = new Out(out);
        solve(s, o);
        o.flush();
        o.close();
    }

    public static void solve(ExtendedScanner sc, Out out) {
        int n = sc.nextInt();
        int[] a = sc.ints(3 * n);
        if (n == 1) {
            out.writeln(a[0] == a[1] && a[1] == a[2] ? 1 : 0);
            return;
        }
        int[][][] ft = new int[5][5][5];
        int[][][] sd = new int[5][5][5];
        for (int i = 0; i < 3; i++) {
            for (int j = i + 1; j < 4; j++) {
                for (int k = j + 1; k < 5; k++) {
                    ft[i][j][k] = -1;
                    sd[i][j][k] = -1;
                    for (int l = 0; l < 5; l++) {
                        if (l != i && l != j && l != k) {
                            if (ft[i][j][k] < 0) ft[i][j][k] = l;
                            else sd[i][j][k] = l;
                        }
                    }
                }
            }
        }
        int max = 0;
        HashMap<IntPair, Integer> set = new HashMap<>();
        for (int i = 0; i < 3; i++) {
            for (int j = i + 1; j < 4; j++) {
                for (int k = j + 1; k < 5; k++) {
                    int s = 0;
                    if (a[i] == a[j] && a[j] == a[k]) {
                        max = 1;
                        s = 1;
                    }
                    IntPair p = new IntPair(a[ft[i][j][k]], a[sd[i][j][k]]);
                    if (set.getOrDefault(p, 0) <= s) set.put(p, s);
                }
            }
        }
        
        for (int x = 1; x < n - 1; x++) {
            HashMap<IntPair, Integer> nxt = new HashMap<>();
            int m = max;
            for (var e : set.entrySet()) {
                IntPair p = e.getKey();
                int score = e.getValue();
                if (score <= m - 1 || score + 1 <= max - 1) continue;
                for (int i = 0; i < 3; i++) {
                    int vi = i == 0 ? p.fst : i == 1 ? p.snd : a[3 * x + i];
                    for (int j = i + 1; j < 4; j++) {
                        int vj = j == 1 ? p.snd : a[3 * x + j];
                        for (int k = j + 1; k < 5; k++) {
                            int vk = a[3 * x + k];
                            int s = score;
                            if (vi == vj && vj == vk) {
                                max = Math.max(max, ++s);
                            }
                            int fs = ft[i][j][k];
                            int sn = sd[i][j][k];
                            int vf = fs == 0 ? p.fst : fs == 1 ? p.snd : a[3 * x + fs];
                            int vs = sn == 0 ? p.fst : sn == 1 ? p.snd : a[3 * x + sn];
                            IntPair q = new IntPair(vf, vs);
                            if (nxt.getOrDefault(q, 0) <= s && s >= max - 1) nxt.put(q, s);
                        }
                    }
                }
            }
            var tmp = nxt; nxt = set; set = tmp;
        }
        for (var e : set.entrySet()) {
            IntPair p = e.getKey();
            int s = e.getValue();
            max = Math.max(max, s + (p.fst == p.snd && p.fst == a[3 * n - 1] ? 1 : 0));
        }
        out.writeln(max);
    }
}


