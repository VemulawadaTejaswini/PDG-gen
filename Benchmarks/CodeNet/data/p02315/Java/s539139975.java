import java.io.*;
import java.util.*;

public class Solution {

  private static int N;
  private static int W;
  private static int[] v;
  private static int[] w;

  public static void main(String[] args) {

/*
    N = 4;
    W = 5;
    v = new int[]{4, 5, 2, 8};
    w = new int[]{2, 2, 1, 3};
*/

    N = 2;
    W = 20;
    v = new int[]{5, 4};
    w = new int[]{9, 10};

    int ret = search(0, W);

    System.out.println(ret);
  }

  /**
    i ?????§???????????????????????¶???????????\????????¨????????????????????§????????????.

    search(i + 1, weight) = max(
     search(i, weight - weight[i]) + value[i],
     search(i, weight)
    )

    ?????°??¢??°??????????????¶: i == N

    @return ??????????????§???.
  */
  public static int search(int i, int weight) {
    if (i == N) {
      return 0;
    } else if (weight < w[i]) {
      return search(i + 1, weight);
    } else {
      return Math.max(
        search(i + 1, weight - w[i]) + v[i],
        search(i + 1, weight)
      );
    }
  }

}