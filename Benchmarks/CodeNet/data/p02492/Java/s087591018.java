
import java.util.Scanner;

public class Main {
	public static void main(String[] args) {
		// ?¨??????\???????????????????????????????????????
		int i = 0, j = 0;
		int[] a, b;
		a = new int[100];
		b = new int[100];
		int[] answer;
		answer = new int[100];
		String[] op;
		op = new String[100];

		while (true) {
			Scanner sc = new Scanner(System.in);
			// ????????????????????????????????´??°?????¨????????????????????????
			a[i] = sc.nextInt();
			op[i] = sc.next();
			b[i] = sc.nextInt();

			if (op[i].equals("+"))
				answer[i] = a[i] + b[i];
			else if (op[i].equals("-"))
				answer[i] = a[i] - b[i];
			else if (op[i].equals("*"))
				answer[i] = a[i] * b[i];
			else if (op[i].equals("/"))
				answer[i] = a[i] / b[i];
			else if (op[i] == "?")
				break;
			else {
				// System.out.println("Unknown Operator");
				break;
			}
			i++;
		}
		for (j = 0; j < i; j++)
			System.out.printf("%d%n", answer[j]);
	}
}