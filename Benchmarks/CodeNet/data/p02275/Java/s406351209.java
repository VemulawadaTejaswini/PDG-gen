import java.util.Scanner;
import java.util.StringJoiner;

class Main {

	static int n;

	public static void main(String[] args) {

		Scanner sc = new Scanner(System.in);
		n = sc.nextInt();
		int[] a = new int[n];		// ??\????????????????????????
		for (int i = 0; i < n; i++) {
			a[i] = sc.nextInt();
		}
		sc.close();

		int[] b = new int[n];		// ????????¨??????
		countingSort(a, b, 10_000);	// 0???<= A[i] <=???10,000

		StringJoiner sj = new StringJoiner(" ");
		for (int i = 0; i < n; i++) {
			sj.add(String.valueOf(b[i]));
		}

		System.out.println(sj);

	}

	private static void countingSort(int[] a, int[] b, int k) {

		int[] c = new int[k+1];

		/* c[i]?????°i???????????°????¨?????????? */
		for (int i = 0; i < n; i++) {
			c[a[i]] ++;
		}

		/* c[i]?????°i??\????????°???????????°????¨?????????? */
		for (int i = 1; i <= k; i++) {
			c[i] += c[i-1];
		}

		for (int j = n-1; j >= 0; j--) {
			b[ c[a[j]]-1 ] = a[j];
			c[a[j]]--;
		}

	}
}