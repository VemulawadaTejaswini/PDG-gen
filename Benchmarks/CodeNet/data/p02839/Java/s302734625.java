    FastScanner in;
    PrintWriter out;
    int MOD = (int)1e9+7;
    long ceil(long a, long b){return (a + b - 1) / b;}
    long gcd(long a, long b){return b == 0 ? a : gcd(b, a % b);}
    long lcm(long a, long b){return a / gcd(a, b) * b;}
    
    void solve() {
        int H = in.nextInt(), W = in.nextInt();
        int[][] A = new int[H][W], B = new int[H][W];
        for(int i = 0; i < H; i++){
            for(int j = 0; j < W; j++){
                A[i][j] = in.nextInt();
            }
        }
        for(int i = 0; i < H; i++){
            for(int j = 0; j < W; j++){
                B[i][j] = in.nextInt();
            }
        }
        
        int MAXD = (H + W) * 80;
        int[] dy = {1, 0};
        int[] dx = {0, 1};
        boolean[][][] dp = new boolean[H][W][2*MAXD+1];
        dp[0][0][(A[0][0] - B[0][0])+MAXD] = true;
        dp[0][0][-(A[0][0] - B[0][0])+MAXD] = true;
        for(int i = 0; i < H; i++){
            for(int j = 0; j < W; j++){
                for(int d = -MAXD; d <= MAXD; d++){
                    if(!dp[i][j][d+MAXD]) continue;
                    for(int k = 0; k < dy.length; k++){
                        int nextY = i + dy[k], nextX = j + dx[k];
                        if(nextY < 0 || nextY >= H || nextX < 0 || nextX >= W) continue;
                        int sub = (A[nextY][nextX] - B[nextY][nextX]);
                        int nextD1 = d + sub;
                        int nextD2 = d - sub;
                        dp[nextY][nextX][nextD1+MAXD] = true;
                        dp[nextY][nextX][nextD2+MAXD] = true;
                    }
                }
            }
        }
        
        int min = Integer.MAX_VALUE;
        for(int d = -MAXD; d <= MAXD; d++){
            if(dp[H-1][W-1][d+MAXD]){
                min = Math.min(min, Math.abs(d));
            }
        }
        out.println(min);
    }
    
