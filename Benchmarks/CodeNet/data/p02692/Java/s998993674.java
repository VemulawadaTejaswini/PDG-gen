import java.io.*;
import java.util.*;

class Solver {
  private static final String[] KEY = new String[]{"A", "B", "C"};

  private static final Map<String, List<Integer>> map = Map.of(
    "AB", List.of(0, 1),
    "BC", List.of(1, 2),
    "AC", List.of(2, 0)
  );

  final int n, initA, initB, initC;
  final String[] ss;
  int[] values;

  Solver(int n, int initA, int initB, int initC, String[] ss) {
    this.n = n;
    this.initA = initA;
    this.initB = initB;
    this.initC = initC;
    this.ss = ss;
  }

  private String select(List<Integer> thisTurnList, List<Integer> nextTurnList) {
    for (int i = 0; i < 2; i++) {
      int inc1 = thisTurnList.get(i);
      int dec1 = thisTurnList.get(1 - i);
      if (values[dec1] <= 0) {
        continue;
      }
      values[inc1]++;
      values[dec1]--;

      for (int j = 0; j < 2; j++) {
        int dec2 = nextTurnList.get(j);
        if (values[dec2] > 0) {
          return KEY[inc1];
        }
      }
        
      values[inc1]--;
      values[dec1]++;
    }
    return null;
  }

  public List<String> solve() {
    List<String> history = new ArrayList<>();
    history.add("Yes");
    values = new int[]{initA, initB, initC};

    for (int i = 0; i < n; i++) {
      String thisTurn = ss[i];
      String nextTurn = i + 1 < n ? ss[i + 1] : thisTurn;
      List<Integer> thisTurnList = map.get(thisTurn);
      List<Integer> nextTurnList = map.get(nextTurn);
      String choice = select(thisTurnList, nextTurnList);
      if (choice == null) {
        return Arrays.asList("No");
      }
      history.add(choice);
    }
    return history;
  }
}

public class Main {
  private static void execute(ContestReader reader, ContestWriter out) {
    int n = reader.nextInt();
    int a = reader.nextInt();
    int b = reader.nextInt();
    int c = reader.nextInt();
    String[] ss = reader.next(n);
    out.printList(new Solver(n, a, b, c, ss).solve());
  }
  
  public static void main(String[] args) {
    ContestReader reader = new ContestReader(System.in);
    ContestWriter out = new ContestWriter(System.out);
    execute(reader, out);
    out.flush();
  }
}

