import java.util.*;
import java.lang.*;
import java.io.*;

public class Main {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        String first_line = scanner.nextLine();
		String second_line = scanner.nextLine();
		String third_line = scanner.nextLine();

        System.out.print(first_line.substring(0,1));
        System.out.print(second_line.substring(1,2));
        System.out.print(third_line.substring(2,3));
    }
}