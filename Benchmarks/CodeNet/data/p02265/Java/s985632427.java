import java.util.Scanner;

// ???????????????????????¨?????????
class Node{
    int value;
    Node prev;
    Node next;

    Node(){

    }

    Node(int value){
        this.value = value;
    }
}

public class Main {

    static Node head;

    // ????§?????????¨??????????????????????????¨
    private static void init(){
        head = new Node();
        head.next = head;
        head.prev = head;
    }

    // ??¨?????¨?????´????????????
    public static void insert(int x){

        Node temp = new Node(x);
        temp.next = head.next;
        temp.prev = head;
        head.next = temp;
        temp.next.prev = temp;
    }

    //?????¨??????????????\?????°delete????????????????????°???????????¶????????????
    public static void delete(int x){

        deleteNode(listSearch(x));
    }

    public static Node listSearch(int x){
        Node cur = head.next;
        while (cur != head && cur.value != x){
            cur = cur.next;
        }
        return cur; // ??????????????????????????°?????????,?¢??????´????°??????????????´??????¨?????¨???
    }

    public static void deleteNode(Node node){

        if (node == head){
            return;
        }

        node.prev.next = node.next;
        node.next.prev = node.prev;

        // ?°?node????????¨?????????null?????\????????\?°?????????¶
        node.prev = null;
        node.next = null;
    }

    public static void deleteFirst(){
        deleteNode(head.next);
    }

    public static void deleteLast(){
        deleteNode(head.prev);
    }

    // ?????°?????¨????´????????????¨????????????????¢?
    private static void printList() {
        int count = 0;
        Node cur = head.next;
        while (cur != head){
            if (count++ > 0){
                System.out.print(" ");
            }
            System.out.print(cur.value);
            cur = cur.next;
        }
        System.out.println();
    }

    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();

        init();

        for (int i=0; i<n; i++){
            String str = sc.next();
            if (str.equals("insert")){
                int key = sc.nextInt();
                insert(key);
            }else if (str.equals("delete")){
                int key = sc.nextInt();
                delete(key);
            }else if (str.equals("deleteFirst")){
                deleteFirst();
            }else {
                deleteLast();
            }
        }

        printList();
    }

}