import java.util.*;
public class Main {
  public static void main(String[] args) {
    Scanner sc = new Scanner(System.in);
    int age = sc.nextInt();
    if (age == 25) System.out.println("Christmas");
    if (age == 24) System.out.println("Christmas Eve");
    if (age == 23) System.out.println("Christmas Eve Eve");
    if (age == 22) System.out.println("Christmas Eve Eve Eve");    
  }
}