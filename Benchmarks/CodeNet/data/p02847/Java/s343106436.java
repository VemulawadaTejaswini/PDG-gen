import java.util.Scanner;
public class Main{
  public static void main(String[] args){
    Scanner scan = new Scanner(System.in);
    String s = scan.next();
    if(s == "SUN") {System.out.println(7);}
    if(s == "MON") {System.out.println(6);}
    if(s == "TUE") {System.out.println(5);}
    if(s == "WED") {System.out.println(4);}
    if(s == "THU") {System.out.println(3);}
    if(s == "FRI") {System.out.println(2);}
    if(s == "SAT") {System.out.println(1);}
    
  }
}
