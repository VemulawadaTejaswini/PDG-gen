    static class Solver 
    {
    	long[] g_1;
    	long[] g_2;
    	
    	long gcd(long a, long b) {		
    		if (b == 0)	
    			return a;
    		return gcd(b, a % b);	
    	}		

    	public void solve(int testNumber, MyInput in, PrintWriter out) {
			int N = in.nextInt();
			long[] A = new long[N];
			for(int i=0; i < N; i++) {
				A[i] = in.nextLong();
			}
			// 前からの累積ＧＣＤ
			g_1 = new long[N];
			g_1[0] = A[0];
			for(int i=1; i < N; i++) {
				g_1[i] = gcd(g_1[i-1], A[i]);
//				out.println("g_1[" + i + "] = " + g_1[i]);
			}
			// 後ろからの累積ＧＣＤ
			g_2 = new long[N];
			g_2[N-1] = A[N-1];
			for(int i=N-2; i >= 0; i--) {
				g_2[i] = gcd(g_2[i+1], A[i]);
//				out.println("g_2[" + i + "] = " + g_2[i]);
			}
			long wk;
			long ans;
			if(N == 2) {
				ans = Math.max(A[0], A[1]);
			} else {
				ans = g_2[1];
				for(int i=1; i < N-1; i++) {
					wk = gcd(g_1[i-1], g_2[i+1]);
					if(ans < wk)	ans = wk;
				}
				if(ans < g_1[N-1])	ans = g_1[N-1];
			}
			out.println(ans);
        }
    }
