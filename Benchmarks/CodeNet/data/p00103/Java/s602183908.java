package aizu_online1;

import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;

class Innings
{
	protected int out;
	protected int[] runner;
	protected int runcount;
	protected int score;

	//?????????????????????
	public Innings()
	{
		out = 0;
		runner = new int[4];
		for(int i = 0; i < 4; i++){
			runner[i] = 0;
		}
		runcount = 0;
		score = 0;

	}
	//???????????°??????????????????????????????
	public boolean Fhantei(){
		if(out == 3){
			return false;
		}
		else{
			return true;
		}
	}
	/*
	 *??\?????????????????????????????°????????????
	 *????????????????????¢????????????????????????
	 * ????????????
	 */
	public void Input(String s){
		if(s.equals("HIT")){
			this.Hit();
		}
		else if(s.equals("HOMERUN")){
			this.Homerun();
		}
		else if(s.equals("OUT")){
			this.Out();
		}

	}

	//????????°?????????????????????
	public void Hit(){
		runner[3] = runner[2];
		runner[2] = runner[1];
		runner[1] = runner[0];
		runner[0] = 1;
		if(runner[3] == 1){
			score++;
		}
	}

	//????????????????????????
	public void Homerun(){
		for(int i = 0; i < 3; i++){
			score = score + runner[i];
			runner[i] = 0;
		}
			score ++;

	}

	public void Out(){
		out++;
	}

}



class Main
{
	public static void main(String[] args)throws IOException
	{
		BufferedReader  br =
			new BufferedReader(new InputStreamReader(System.in));

		int num = Integer.parseInt(br.readLine());
		Innings innings = new Innings();
		for(int i = 0; i < num; i++){
			while(innings.Fhantei()){
				String str = br.readLine();
				innings.Input(str);
			}
			System.out.println(innings.score);
			innings = new Innings();
		}
	}

}