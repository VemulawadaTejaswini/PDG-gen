import java.util.Scanner;

public class Main{
	public static void main(String[] args){
		Scanner scan = new Scanner(System.in);
		while( true ){
			int w = Integer.parseInt(scan.next());
			int h = Integer.parseInt(scan.next());
			if( w == 0 && h == 0 ){
				break;
			}
			MAP[][] state = new MAP[w][h];
			for( int j = 0; j < h; j++ ){
				for( int i = 0; i < w; i++ ){
					state[i][j] = new MAP();
					state[i][j].Reset();
				}
			}
			//??????????????\??????
			int temp = 0;
			for( int j = 0; j < h-1; j++ ){
				for( int i = 0; i < w-1; i++ ){
					//??????????£?
					temp = Integer.parseInt(scan.next());
					state[i][j].r = temp;
					state[i+1][j].l = temp;
				}
				for( int i = 0; i < w; i++ ){
					//??????????£?
					temp = Integer.parseInt(scan.next());
					state[i][j].d = temp;
					state[i][j+1].u = temp;
				}
			}
			//????????????????????????????£?
			for( int i = 0; i < w-1; i++){
				temp = Integer.parseInt(scan.next());
				state[i][h-1].r = temp;
				state[i+1][h-1].l = temp;
			}
			
			System.out.println(Move(state, 0, 0, w, h));
			
			
		}
		
		return;
	}
	
	public static int Move(MAP state[][], int x, int y, int w, int h){
		if( state[x][y].step >= 0 ){
			return state[x][y].step;
		}
		int temp = 0;
		int min = 0;
		//System.out.printf("%d %d\n", x, y);
		if( x == w-1 && y == h-1 ){
			return 1;
		}
		if( state[x][y].u == 0 ){
			//???????£?????¨???????
			state[x][y].u = 2;
			state[x][y-1].d = 2;
			temp = Move(state, x, y-1, w, h);
			//???????£?????????????
			state[x][y].u = 0;
			state[x][y-1].d = 0;
			if( temp > 0 ){
				temp++;	//??????????§??????????
				if( temp < min || min <= 0 ){
					min = temp;
				}
			}
		}
		if( state[x][y].l == 0 ){
			//???????£?????¨???????
			state[x][y].l = 2;
			state[x-1][y].r = 2;
			temp = Move(state, x-1, y, w, h);
			//???????£?????????????
			state[x][y].l = 0;
			state[x-1][y].r = 0;
			if( temp > 0 ){
				temp++;	//??????????§??????????
				if( temp < min || min <= 0 ){
					min = temp;
				}
			}
		}
		if( state[x][y].r == 0 ){
			//???????£?????¨???????
			state[x][y].r = 2;
			state[x+1][y].l = 2;
			temp = Move(state, x+1, y, w, h);
			//???????£?????????????
			state[x][y].r = 0;
			state[x+1][y].l = 0;
			if( temp > 0 ){
				temp++;	//??????????§??????????
				if( temp < min || min <= 0 ){
					min = temp;
				}
			}
		}
		if( state[x][y].d == 0 ){
			//???????£?????¨???????
			state[x][y].d = 2;
			state[x][y+1].u = 2;
			temp = Move(state, x, y+1, w, h);
			//???????£?????????????
			state[x][y].d = 0;
			state[x][y+1].u = 0;
			if( temp > 0 ){
				temp++;	//??????????§??????????
				if( temp < min || min <= 0 ){
					min = temp;
				}
			}
		}
		state[x][y].step = min;
		return min;
	}
	
}

class MAP{
	//1???????£????????????????????£????2???
	int u;
	int l;
	int r;
	int d;
	int step;	//??´???????????§???????°??§?????????°???
	public void Reset(){
		u = 1;
		l = 1;
		r = 1;
		d = 1;
		step = -1;
		return;
	}
}