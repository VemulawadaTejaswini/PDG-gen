import java.util.*;
 
class Main {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        String a = sc.nextLine();
        String b = sc.nextLine();
      
      if(a.charAt(0)=='H'){
        system.out.print(b.charAt(0));
      }if(a.charAt(0)=='D'){
        if(b.charAt(0)=='D'){
           system.out.print('H');
        }if(b.charAt(0)=='H'){
           system.out.print('D');
        }
        
      }
    }
}