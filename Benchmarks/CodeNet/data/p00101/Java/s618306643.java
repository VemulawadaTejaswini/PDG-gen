import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;

public class Main {
	public static void main(String[] args) throws IOException {
		BufferedReader br = new BufferedReader(new
		InputStreamReader(System.in));

		int sentenceNum = Integer.parseInt(br.readLine()); // ???????????°??????????????????

		String str;
		String[] splitedStr;

		for (int i = 0; i < sentenceNum; i++) {
			str = br.readLine(); // ?????????????????????
			splitedStr = str.split(" "); // ?????????????????????
			fix(splitedStr); // ????????????????????????"Hoshino"???"Hoshina"????????´??????
			write(splitedStr); // ?????????????????´???
		}

	}

	private static void write(String[] splitedStr) {
		for (int i = 0; i < splitedStr.length; i++) {
			System.out.print(splitedStr[i]);
			if (i != (splitedStr.length - 1))
				System.out.print(" ");
		}
		System.out.print("\n");
	}

	private static void fix(String[] splitedStr) {
		for (int i = 0; i < splitedStr.length; i++) {
			if (splitedStr[i].equals("Hoshino")) {
				splitedStr[i] = "Hoshina";
			}
		}
	}
}