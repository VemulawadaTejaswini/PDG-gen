import java.util.Arrays;
import java.util.Scanner;

public class Main {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int N = sc.nextInt();
		int A[] = new int[N+1];
		for(int i = 1; i <= N; i++) {
			A[i] = sc.nextInt();
		}
		long result = 0;
		for(int i = 1; i <= N-1; i++) {
			for(int j = i+1; j <= N; j++) {
				int a;
				a = A[i] ^ A[j];
				result += a;
				if(result >= 1000000007)
					result %= 1000000007;
			}
		}
		System.out.println(result);
	}

}
