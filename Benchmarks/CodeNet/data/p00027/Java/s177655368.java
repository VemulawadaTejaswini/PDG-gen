import java.util.Scanner;

public class Main {

	public static void main(String[] args) {
		// TODO ?????????????????????????????????????????????
		Scanner sc = new Scanner(System.in);

		while (true) {
			int m = sc.nextInt();
			int d = sc.nextInt();
			if (m == 0 || d == 0) break;
			String ans = yoobi( sumofdays(m, d) );
			System.out.println(ans);
		}
	}

	// 1???1??\??????????????????m????????\?????§?????\??°
	private static int sumofdays(int m) {
		if (m == 0) return 0;
		switch (m) {
		case 2:
			return sumofdays(m-1) + 29;
		case 4:
		case 6:
		case 9:
		case 11:
			return sumofdays(m-1) + 30;
		default:
			return sumofdays(m-1) + 31;
		}
	}

	// 1???1??\??????m???d??\?????§?????\??°
	public static int sumofdays(int m, int d) {
		return sumofdays(m-1) + d;
	}

	public static String yoobi(int d) {
		int r = d % 7;
		switch (r) {
		case 1:
			return "Thursday";
		case 2:
			return "Friday";
		case 3:
			return "Saturday";
		case 4:
			return "Sunday";
		case 5:
			return "Monday";
		case 6:
			return "Tuesday";
		default :
			return "Wednesday";
		}
	}
}