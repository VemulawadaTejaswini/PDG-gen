import java.util.*;
public class Main{
  public static void main(String[] args){
    Scanner I = new Scanner(System.in);
    int a =I.nextInt();
    int b =I.nextInt();
    if(a>=b){
      for(int i=0;i<a;i++){
        int c = 10*c;}
      System.out.println((c-1)*b/9);}
    else{
      for(int i=0;i<b;i++){
        int d = 10*d;}
      System.out.println((d-1)*a/9);}
  }
}
      