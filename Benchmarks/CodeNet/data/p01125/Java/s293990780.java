	public static void moveRobo(String direction,int stepCount){
		for (int m = 0; m<stepCount; m++){

			switch (direction) {
			case "N"://y+a
				roboY++;
				map[roboY][roboX]=false;
				break;
			case "E"://x+a
				roboX++;
				map[roboY][roboX]=false;
				break;
			case "W"://x-a
				roboX--;
				map[roboY][roboX]=false;
				break;
			case "S"://y-a
				roboY--;
				map[roboY][roboX]=false;
				break;
			default://?¶???????????????£????
				break;
			}

		}
	}

