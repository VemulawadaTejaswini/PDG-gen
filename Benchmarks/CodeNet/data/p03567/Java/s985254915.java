import java.util.*;
public class Main{
        public static void main(String[] args){
                Scanner sc = new Scanner(System.in);
                String s = sc.next();
                boolean ebishu = true;
                for(int i = 0 ; i < s.length() - 1 ; i++){
                        if(s.charAt(i) == 'A' && s.charAt(i + 1) == 'C') ebishu = false;
                }
                if(ebishu){
                        System.out.println("No");
                }else{
                        System.out.println("Yes");
                }
        }
}
