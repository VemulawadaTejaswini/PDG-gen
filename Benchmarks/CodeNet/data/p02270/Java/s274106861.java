import java.io.*;
 
class Main {
    public static void main(String[] args) {
        
        try {
                BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
            String str = br.readLine();
			int n = Integer.parseInt(str.split(" ")[0]);
			int k = Integer.parseInt(str.split(" ")[1]);
			int[] nimotu = new int[n];
			int totalWeight = 0;
			int maxweight = 0;
			for (int i = 0; i < n; i++) {
				nimotu[i] = Integer.parseInt(br.readLine());
				totalWeight += nimotu[i];
				if(nimotu[i] > maxweight){
					maxweight = nimotu[i];
				}
			}
			int aver = totalWeight / k;
			int settingVal= 0;
			if(maxweight < aver){
				settingVal = aver;
			}else{
				settingVal = maxweight;
			}
			int tracknum;
			while(settingVal <= totalWeight){
				int weight = 0;
				tracknum = 1;
				for(int i = 0;i < n && tracknum <= k;) {
					// ????????????????????????????????°?????????
					if(weight + nimotu[i] <= settingVal){
						weight += nimotu[i];
						i++;
					//???????????????????????????????????????????????????????????????????????£?????????????????°?¬???????????????????
					} else {
						weight = 0;
						tracknum++;
					}
				}
				if(tracknum <= k){
					System.out.println(settingVal);
					break;
				}
				settingVal++;
			}
		} catch (IOException e) {
			System.out.println("error");
		}
	}
}