	void run() {
		for (;;) {
			int N = sc.nextInt();
			int M = sc.nextInt();
			if ((N | M) == 0) {
				return;
			}
			boolean[] virus = new boolean[N];
			virus[0] = true;
			Info[] info = new Info[M];
			for (int i = 0; i < M; i++) {
				info[i] = new Info(sc.nextInt(), sc.nextInt() - 1,
						sc.nextInt() - 1);
			}
			Arrays.sort(info);
			for (int i = 0; i < M; i++) {
				Info now = info[i];
				if (virus[now.s]) {
					virus[now.d] = true;
				}
			}
			int cnt = 0;
			for (int i = 0; i < N; i++) {
				if (virus[i]) {
					cnt++;
				}
			}
			System.out.println(cnt);
		}
	}

	class Info implements Comparable<Info> {
		int time;
		int s;
		int d;

		/**
		 * @param time
		 * @param s
		 * @param d
		 */
		public Info(int time, int s, int d) {
			super();
			this.time = time;
			this.s = s;
			this.d = d;
		}

		@Override
		public int compareTo(Info arg0) {
			return this.time - arg0.time;
		}

	}

