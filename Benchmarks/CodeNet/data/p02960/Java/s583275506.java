    static int [][]memo;
    static char[]s;
    static int modo = (int) (1e9+7);
    static int dp(int mod,int idx){
        if (idx==s.length){
            return mod==5?1:0;
        }
        if (memo[mod][idx]!=-1)return memo[mod][idx];
        int nmod=mod*10;
        nmod%=13;
        int ans =0;
        if (s[idx]=='?')
        for (int i =0;i<10;i++){
            ans+=dp((nmod+i)%13,idx+1);
            ans%=modo;
        }
        else{
            ans=dp((nmod+(s[idx]-'0'))%13,idx+1)%modo;
        }
        return memo[mod][idx]=ans;
    }
