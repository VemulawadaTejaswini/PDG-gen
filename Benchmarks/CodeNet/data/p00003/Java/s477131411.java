import java.util.Scanner;

public class Main {
	static Scanner sc = new Scanner(System.in);
	static String[][] triangles;
	static int num;

	public static void main(String[] args) {
		input();
		for(int count=0; count < 3; count++) {
			if(judge(triangles[count])) {
				System.out.println("YES");
			} else {
				System.out.println("NO");
			}
		}
	}

	//???????????????????????????????????¨??????????????????
	public static void input() {
		num = Integer.parseInt(sc.nextLine());
		triangles = new String[num][3];
		for(int count = 0; count < num; count++) {
			triangles[count] = sc.nextLine().split(" ");
		}
	}

	//???????????????????????°????????????????????´?§?????§???¢????????????
	public static boolean judge(String[] triangles) {
		int a = Integer.parseInt(triangles[0]);
		int b = Integer.parseInt(triangles[1]);
		int c = Integer.parseInt(triangles[2]);
		int box;

		if(b > a) {
			box = a;
			a = b;
			b = box;
		}

		if(c > a) {
			box = a;
			a = c;
			c = box;
		}

		if((a * a) == (b * b) + (c * c)) {
			return true;
		}

		return false;
	}

}