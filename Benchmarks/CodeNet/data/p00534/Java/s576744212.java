import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;



public class Main {

	static int[] C, D;
	static int M, N; //N??????M??\
	static int[][] memo;

	public static void main(String args[]) throws IOException {
		BufferedReader in = new BufferedReader(new InputStreamReader(System.in));
		String[] strs = in.readLine().split(" ");
		N = Integer.parseInt(strs[0]);
		M = Integer.parseInt(strs[1]);
		memo = new int[N][M];
		D = new int[N];
		for (int i = 0; i < N; i++) {
			D[i] = Integer.parseInt(in.readLine());
		}
		C = new int[M];
		for (int i = 0; i < M; i++) {
			C[i] = Integer.parseInt(in.readLine());
		}

		System.out.println(func(0, 0));
	}

	static int func(int town, int day) {
		//??????town?????????day??\????????¨???????????????????????????????????´??????????°???????????????¢??°
		//?????´??????????°?????????????????????¨????´¢
		if (day == M) {
			if (town == N) {
				return 0;
			}
			return Integer.MAX_VALUE >> 1;
		}
		if (town == N) {
			return 0;
		}
		//??????????????????
		if (memo[town][day] != 0) return memo[town][day]+1;
		int T1 = func(town, day+1); //??¨?????£????????¨???
		int T2 = func(town+1, day+1) + D[town]*C[day]; //???????????¨???
		if (T1 < T2) {
			memo[town][day] = T1 - 1;
			return T1;
		}
		memo[town][day] = T2 - 1;
		return T2;
	}
}