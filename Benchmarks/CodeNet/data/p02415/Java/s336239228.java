

import java.util.ArrayList;
import java.util.List;
import java.util.Scanner;


public class Main {

    public static void main(String[] args) {
        
        Scanner sc = new Scanner(System.in);
        
        List<String> str = new ArrayList<>();
        
        str.add(sc.nextLine());
      
        for(String e : str){
            System.out.println(e.toUpperCase());
            System.out.println(" ");
        }
          
        
    }
}



        
    

       

   


