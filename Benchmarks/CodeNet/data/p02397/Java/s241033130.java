import java.util.Scanner;

class Main{
     public static void main(String[] args){

        Scanner as = new Scanner(System.in);
        int x = as.nextInt();
        int y = as.nextInt();

        while(x!=0 && y!=0){
           System.out.println(x+" "+y);
           x = as.nextInt();
           y = as.nextInt();
 
 
   }
 }
 
}