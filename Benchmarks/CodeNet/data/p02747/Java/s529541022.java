import java.util.*;

public class Main {
		public static void main(String[] args) {
		
	   Scanner sc = new Scanner(System.in);
	   
	   String S = sc.next();
	   
	   if(S.equals("hi")) {
		   System.out.println("Yes");
	   }else if(S.equals("hihi")) {
		   System.out.println("Yes");
	   }else if(S.equals("hihihi")) {
		   System.out.println("Yes");
	   }else if(S.equals("hihihihi")) {
		   System.out.println("Yes");
	   }else if(S.equals("hihihihihi")) {
		   System.out.println("Yes");
	   }else {
		   System.out.println("No");
	   }
	   }
}

