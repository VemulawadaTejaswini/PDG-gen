import java.util.Scanner;

public class Main {
	public static void main(String[] args)
	{
		Scanner scan = new Scanner(System.in);
		int count = scan.nextInt();
		int[] x = new int[count];
		int[] y = new int[count];
		double p;
		double p_1 = 0;
		double p_2 = 0;
		double p_3 = 0;
		double p_inf = 0;
		
		for(int i = 0;i < count;i++)
		{
			x[i] = scan.nextInt();
		}
		for(int i = 0;i < count;i++)
		{
			y[i] = scan.nextInt();
		}
		
		for(int i = 0;i < count;i++)
		{
			p = Math.abs(x[i] - y[i]);
			p_1 += p;
			p_2 += Math.pow(p, 2);
			p_3 += Math.pow(p, 3);
			if(p > p_inf){p_inf = p;}
		}
		p_2 = Math.sqrt(p_2);
		p_3 = Math.cbrt(p_3);
		
		System.out.printf("%f\n%f\n%f\n%f\n", p_1, p_2,p_3,p_inf);
	}
}