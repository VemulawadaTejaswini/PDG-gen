	public static double CalcD(String X[], String Y[], double P){
		double sig = 0;
		for(int i = 0; i < X.length; i++){
			sig += Math.pow(Math.abs(Integer.valueOf(X[i]) - Integer.valueOf(Y[i])), P);
		}
		double d = Math.pow(sig, 1 / P);
		return d;
	}
