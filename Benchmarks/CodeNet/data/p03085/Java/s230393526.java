public class Main {
	public static void main(String args[]) {
		java.util.Scanner sc = new java.util.Scanner(System.in);
		String b = sc.next();

		if (b.equals("A")) {
			System.out.println("T");
		}

		if (b.equals("C")) {
			System.out.println("G");
		}

		if (b.equals("T")) {
			System.out.println("A");
		}

		if (b.equals("G")) {
			System.out.println("C");
		}

	}
}
