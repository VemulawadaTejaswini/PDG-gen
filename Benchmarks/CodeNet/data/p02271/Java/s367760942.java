
import java.text.DecimalFormat;
import java.util.stream.LongStream;
import java.util.stream.IntStream;
import java.io.*;
import java.util.*;

public class Main {

    public static void main(String[] args) {
        FastScanner sc = new FastScanner();
        PrintWriter out = new PrintWriter(System.out);

        AOJ problem = new AOJ(sc);
        problem.solve(out);

        out.flush();
    }

}

class AOJ {

    int n, q;
    int[] A, m;

    AOJ(FastScanner sc) {
        n = sc.nextInt();
        A = IntStream.range(0, n).map(i -> sc.nextInt()).toArray();
        q = sc.nextInt();
        m = IntStream.range(0, q).map(i -> sc.nextInt()).toArray();
    }

    void solve(PrintWriter out) {
        int N = (int) Math.pow(2, n);
        List<Integer> memo = new ArrayList<>(N);
        for (int i = 0; i < N; i++) {
            BitSet bs = BitSet.valueOf(new long[]{i});
            int sum = 0;
            for (int index = 0; index < n; index++) {
                if (bs.get(index)) sum += A[index];
            }
            memo.add(sum);
        }
        for (int i = 0; i < q; i++) {
            if (memo.contains(m[i])) out.println("yes");
            else out.println("no");
        }
    }

}

