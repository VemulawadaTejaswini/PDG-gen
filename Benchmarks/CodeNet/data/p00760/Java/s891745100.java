import java.util.Scanner;


public class Main{
	static int END_YEAR = 999;
	static int END_MONTH = 10;
	static int MONTH_BIG = 20;
	static int MONTH_SMALL = 19;
	static int YEAR_ALLBIG_DAYS = MONTH_BIG*10;	//??\????????´?????\??°
	static int YEAR_ORDINAL_DAYS = MONTH_BIG*5+MONTH_SMALL*5;	//??????????????´?????\??°
	static int YEARS_LOOP = YEAR_ALLBIG_DAYS + YEAR_ORDINAL_DAYS*2;	//?????´??????

	public static void main(String[] args) {
try{

	//Scanner scan = new Scanner(new File("in.txt"));
	Scanner scan = new Scanner(System.in);

	int number = scan.nextInt();

	for(int i = 0; i < number; i++) {
		System.out.println(getAnswer(scan.nextInt(), scan.nextInt(), scan.nextInt()));
	}

	scan.close();

}catch(Exception e) {
	e.printStackTrace();
}

	}

	public static int getAnswer(int _y, int _m, int _d) {
		int sum = 0;

		//????????\????????§????????´??\????????\??°????¨?????????????(????????§?????????????????´????????¨??????????????§)
		sum += getYearDates(_y,_m,_d);
		_y++;
		for(int i = _y; !isSpecialYear(i) && _y<=END_YEAR; i++) {
			sum += isSpecialYear(i)? YEAR_ALLBIG_DAYS:YEAR_ORDINAL_DAYS;
			_y++;
		}

		//????????\??°????¨?????????????
		if(END_YEAR > _y) {
			sum += YEARS_LOOP * ((END_YEAR-_y)/3);
			_y = END_YEAR;
		}

		//?????????999/10/20?????§???????????\??°????¨?????????????
		if(_y==END_YEAR) sum += YEAR_ALLBIG_DAYS;

		//????¨??????????
		return sum;
	}

	public static int getYearDates(int _y, int _sm, int _sd) {
		int THIS_YEAR_MONTH_BIG = MONTH_BIG;
		int THIS_YEAR_MONTH_SMALL = (isSpecialYear(_y))? MONTH_BIG:MONTH_SMALL;

		int sum = 0;
		//????????????
		sum += ((isBigMonth(_sm))? THIS_YEAR_MONTH_BIG:THIS_YEAR_MONTH_SMALL) - _sd + 1;
		//????????\???
		for(int i = _sm+1; i <= END_MONTH; i++) {
			sum += ((isBigMonth(i))? THIS_YEAR_MONTH_BIG:THIS_YEAR_MONTH_SMALL);
		}

		return sum;
	}

	public static boolean isSpecialYear(int _y) {
		return (_y%3) == 0;
	}

	public static boolean isBigMonth(int _m) {
		return (_m%2) == 1;
	}
}