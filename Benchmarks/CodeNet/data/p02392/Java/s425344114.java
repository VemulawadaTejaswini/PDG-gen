import java.util.*;

class Range {
    public static void main(String...args) {
        final Scanner sc = new Scanner(System.in);
        final int a = sc.nextInt();
        final int b = sc.nextInt();
        final int c = sc.nextInt();
        System.out.println(a < b && b < c ? "Yes" : "No");
    }
}
public class Main {
    public static void main(String... args) {
        Range.main();
    }
}