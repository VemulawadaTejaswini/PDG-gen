import java.util.Scanner;

public class Main{
	public static void main(String[] args){
		int x,y,s,i,j,max;	
		Scanner in = new Scanner(System.in);
		
		x = in.nextInt(); //??§?¨????
		y = in.nextInt(); //??°?¨????
		s = in.nextInt(); //??§????¨???????
		
		while(x != 0){
			max = 0;

			for(i=1; i<s; i++){
				for(j=1; j<s; j++){
					if(TotalPrice.calcTotalPrice(x, i, j) == s){
						max = Math.max(TotalPrice.calcTotalPrice(y, i, j), max);
					}
				}
			}
			
			System.out.println(max);
			
			x = in.nextInt();
			y = in.nextInt();
			s = in.nextInt();
		}
		
		in.close();
	}
}


