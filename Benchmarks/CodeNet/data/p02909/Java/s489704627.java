import java.util.Scanner;
public class Main{
  public static void main(String args[]){
    Scanner sc = new Scanner(System.in);
    String = a = sc.next();
    if(a == "Sunny"){
      System.out.println("Cloudy");
    }else if(a == "Cloudy"){
       System.out.println("Rainy");
    }else if(a == "Rainy"){
       System.out.println("Sunny");
    }
  }
}