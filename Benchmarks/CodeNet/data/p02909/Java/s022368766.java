import java.util.*;
public class Main {
	public static void main(String[] args){
		Scanner sc = new Scanner(System.in);
		// 整数の入力
		String s = sc.next();
		// 出力
	if(s=='Sunny'){
       System.out.println('Cloudy');
    }else if(s=='Rainy'){
     System.out.println('Sunny');
    }else{
     System.out.println('Rainy');
    }
	}
}