import java.util.*;
 
class Main {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        String s = sc.nextInt();
      	
      	if (s = "Sunny") {
          System.out.print("Cloudy");
        } else if (s = "Cloudy") {
          System.out.print("Rainy");
        } else {
          System.out.print("Sunny");
        }       
    }
}