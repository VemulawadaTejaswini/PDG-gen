import java.util.Scanner;
class main{
public static void Main(){
Scanner scanner=new Scanner(System.in);
int num=scanner.nextInt();
if(num%3==0){
  System.out.println("Rainy");
}else if(num%3==1){
  System.out.println("Sunny");
}else if(num%3==2){
  System.out.println("Cloudy");
}
}
}
