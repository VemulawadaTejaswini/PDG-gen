import java.util.Scanner;

public class Main {

	public static void main(String[] args) {

		// ?????????????????\????????????
		Scanner sc = new Scanner(System.in);

		while (true) {

			int num = sc.nextInt();
			int sum = sc.nextInt();

			if (num == 0 && sum == 0)
				break;

			// ??????????????????????????°??¨????????¶????????????????????????????????°??????????????????????????°
			int pnt1;
			int pnt2;
			int pnt3;
			int cnt = 0;

			for (pnt1 = 1; pnt1 <= num - 2; pnt1++) {

				for (pnt2 = pnt1 + 1; pnt2 <= num - 1; pnt2++) {

					for (pnt3 = pnt1 + 2; pnt3 <= num; pnt3++) {

						if (pnt1 + pnt2 + pnt3 == sum) {

							if (pnt2 == pnt3) {
								cnt += 0;
							} else {
								cnt++;
							}
						}
					}
				}
			}
			System.out.println(cnt);
		}
		sc.close();
	}

}