import java.util.*;

public class Main{
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        sc.nextLine();
        String[] s = new String[a];
        for(int i = 0;i<a;i++){
            s[i] = sc.nextLine();
        }
        for(int i = 0;i<a;i++){
            System.out.println(s[i]);
            System.out.println(s[i]);
        }
    }
}