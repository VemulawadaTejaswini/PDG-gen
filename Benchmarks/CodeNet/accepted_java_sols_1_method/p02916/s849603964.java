import java.util.Arrays;
import java.util.Scanner;

public class Main{

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int N = sc.nextInt();
		int A[] = new int[N];
		for(int i = 0; i < N; i++) {
			A[i] = sc.nextInt() - 1;
		}
		int B[] = new int[N];
		for(int i = 0; i < N; i++) {
			B[i] = sc.nextInt();
		}
		int C[] = new int[N-1];
		for(int i = 0; i < N-1; i++) {
			C[i] = sc.nextInt();
		}
		int result = 0;
		for(int i = 0; i < N; i++) {
			result += B[A[i]];
			if(1 <= i && A[i-1] == A[i]-1) {
				result += C[A[i-1]];
			}
		}
		System.out.println(result);
		sc.close();
	}

}
