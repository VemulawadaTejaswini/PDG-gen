
import java.util.Scanner;

public class Main {
	public static void main(String[] args) {

		Scanner stdIn = new Scanner(System.in);

		// ?????????????????????????????????
		stdIn.useDelimiter("\r");

		String word = stdIn.next();

		// ??????????????????????????§?°??????????
		// String?????????char????????????????????????????????????????????????

		char[] cw = word.toCharArray();
		for (int i = 0; i < cw.length && cw.length < 1200; i++) {
			if (Character.isUpperCase(cw[i])) {
				cw[i] = Character.toLowerCase(cw[i]);
			} else {
				cw[i] = Character.toUpperCase(cw[i]);
			}
		}
		for (char charValue : cw) {
			System.out.print(charValue);
		}
		stdIn.close();
	}
}