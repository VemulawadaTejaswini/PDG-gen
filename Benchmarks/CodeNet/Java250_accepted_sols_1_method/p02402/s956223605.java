import java.util.Scanner;

public class Main {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int n = sc.nextInt();
		int intArray[] = new int[n];
		for(int i = 0; i < n; i++) {
			intArray[i] = sc.nextInt();
		}
		// ????°????????±???????????????°??????
		int min = 0;
        for(int i = 1; i < n; i++) {
            if(intArray[i] < intArray[min]) {
                min = i;
            }
        }
        // ?????§???????±???????????????°??????
        int max = 0;
        for(int i = 1; i < n; i++) {
        	if(intArray[max] < intArray[i]) {
        		max = i;
        	}
        }
        // ????¨????????±???????????????°??????
		long sum = 0;
		for(int i = 0; i < n; i++) {
			sum += intArray[i];
		}
		System.out.println(intArray[min] + " " + intArray[max] + " " + sum);
	}

}