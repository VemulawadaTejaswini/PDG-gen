public class Main {
    public static void main(String[] args) {

        // ??????????????????????????????.
        // ???????????????????????´??° (?????°??¨?????°) ???????????????.
        java.util.Scanner sc = new java.util.Scanner(System.in);
        int n = sc.nextInt();
        int m = sc.nextInt();
        int l = sc.nextInt();

        // ?????????????´????????????????????????????.
        // ???????????????????????´??° (?????????????´?) ???????????????.
        int[][] matrix1 = new int[n][m];
        int[][] matrix2 = new int[m][l];
        for (int i = 0; i < n; i ++) {
            for (int j = 0; j < m; j ++) { matrix1[i][j] = sc.nextInt(); }
        }
        for (int i = 0; i < m; i ++) {
            for (int j = 0; j < l; j ++) { matrix2[i][j] = sc.nextInt(); }
        }

        // ???????????????????????????.
        long sum = 0;
        for (int i = 0; i < n; i ++) {
            for (int j = 0; j < l; j ++) {
                sum = 0;
                for (int k = 0; k < m; k ++) { sum += matrix1[i][k] * matrix2[k][j]; }
                System.out.print(sum);
                if (j != l - 1) { System.out.print(" "); }
            }
            System.out.println();
        }
    }
}