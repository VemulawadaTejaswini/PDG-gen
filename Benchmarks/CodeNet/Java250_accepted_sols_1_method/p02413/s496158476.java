import java.util.Arrays;
import java.util.Scanner;



public class Main{
	public static void main(String[] args){
		Scanner scan=new Scanner(System.in);
		int r=scan.nextInt();
		int c=scan.nextInt();
		int[][] matrix_rc=new int[r+1][c+1];
		int sum_r;
		int[] sum_c=new int[c+1];
		Arrays.fill(sum_c, 0);
		for(int i=0;i<r;i++){
			sum_r=0;//??????????????§?????????
			for(int j=0;j<c+1;j++){
				if(j!=c){
				matrix_rc[i][j]=scan.nextInt();
				sum_r+=matrix_rc[i][j];}
				else if(j==c){
					matrix_rc[i][j]=sum_r;
				}//c+1????????§??????????????????
				sum_c[j]+=matrix_rc[i][j];//j????????????????¶???????????????????????????£??\
			}
		}
		for(int j=0;j<c+1;j++){
			matrix_rc[r][j]=sum_c[j];
		}//r+1?????????????????£??\
		for(int i=0;i<r+1;i++){
			for(int j=0;j<c+1;j++){
				System.out.print(matrix_rc[i][j]);
				if(j<c){System.out.print(" ");}
			}System.out.println();
		}
		scan.close();
}


}