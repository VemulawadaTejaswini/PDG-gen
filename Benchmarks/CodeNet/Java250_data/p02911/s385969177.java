	public static String joinInt(int[] a, String separator) {
		StringBuilder sb = new StringBuilder();
		for (int i = 0; i < a.length; i++) {
			if (i > 0) {
				sb.append(separator);
			}
			sb.append(a[i]);
		}
		return sb.toString();		
	}

	static int[] nextIntArray(Scanner sc, int N) {
		int[] arr = new int[N];
		for (int i = 0; i < N; i++) {
			arr[i] = sc.nextInt();
		}
		return arr;
	}

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int N = sc.nextInt();
		int K = sc.nextInt();
		int Q = sc.nextInt();
		int[] ans = new int[N];
		for (int i = 0; i < Q; i++) {
			int a = sc.nextInt();
			ans[a - 1]++;
		}

		StringBuilder sb = new StringBuilder();
		for (int i = 0; i < N; i++) {
			boolean ok = K - Q + ans[i] > 0;
			sb.append(ok ? "Yes" : "No");
			sb.append("\n");
		}
		System.out.println(sb.toString());
	}
