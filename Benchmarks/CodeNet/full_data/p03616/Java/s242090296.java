import java.io.IOException;
import java.io.InputStream;
import java.io.PrintWriter;
import java.util.NoSuchElementException;
import java.util.function.IntUnaryOperator;
import java.util.function.LongUnaryOperator;
import static java.lang.Math.max;
import static java.lang.Math.min;



public class Main {
    public static void main(String[] args) {
        StringBuilder out = new StringBuilder();
        solve(out);
        PrintWriter pw = new PrintWriter(System.out);
        pw.println(out);
        pw.flush();
        pw.close();
    }

    public static void solve(StringBuilder out) {
        long x = In.nl();
        int k = In.ni();
        long[] r = new long[k + 2];
        r[0] = 0;
        for (int i = 1; i <= k; i++) r[i] = In.nl();
        r[k + 1] = (long) 1e9;
        int q = In.ni();
        long u = 0, d = x;
        long xu = 0, xd = x;
        for (int i = 0, j = 0; i < q; i++) {
            long t = In.nl();
            long a = In.nl();
            int lf = 0, rg = k + 1;
            while (rg - lf > 1) {
                int md = (lf + rg) >> 1;
                if (r[md] <= t) lf = md;
                else rg = md;
            }
            while (j < lf) {
                j++;
                if ((j & 1) == 1) {
                    u = max(u, r[j] - r[j - 1] - xu + u);
                    xu = max(0, xu - (r[j] - r[j - 1]));
                    xd = max(0, xd - (r[j] - r[j - 1]));
                } else {
                    d = min(d, d - (r[j] - r[j - 1]) + (x - xd));
                    xu = min(x, xu + (r[j] - r[j - 1]));
                    xd = min(x, xd + (r[j] - r[j - 1]));
                }
            }
            a = a <= u ? xu : a >= d ? xd : xu + a - u;
            out.append((j & 1) == 0 ? max(0, a - (t - r[j])) : min(x, a + (t - r[j]))).append('\n');
        }
    }
}


