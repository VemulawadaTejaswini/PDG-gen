	void solve() throws IOException{
		int n = in.nextInt();
		int m = in.nextInt();
		ArrayList<Edge>[] node = new ArrayList[n];
		for(int i=0; i<n; i++) node[i] = new ArrayList<>();
		final int inf = Integer.MAX_VALUE/2;
		boolean[] used = new boolean[m*2];
		Edge[] edge = new Edge[m*2];
		for(int i=0; i<m; i++){
			final int p = in.nextInt()-1;
			final int q = in.nextInt()-1;
			final int c = in.nextInt();
			node[p].add(edge[i*2] = new Edge(q, c, i*2));
			node[q].add(edge[i*2+1]=new Edge(p, c, i*2+1));
		}
		for(int i=0; i<n; i++){
			Collections.sort(node[i]);
		}
		int[] used_v = new int[n];
		Edge left = new Edge(-1, 0, 0);
		Queue<Edge> qu = new ArrayDeque<>();
		List<TreeSet<Integer>> graph = new ArrayList<>(n);
		for(int i=0; i<n; i++) graph.add(new TreeSet<>());
		for(Edge se: edge){
			if(used[se.id]) continue;
			used[se.id] = true;
			qu.add(se);
			graph.add(new TreeSet<>());
			final int vi = graph.size()-1;
			used_v[se.to] = vi;
			while(!qu.isEmpty()){
				Edge e = qu.poll();
				edge(graph, vi, e.to);
				left.c = e.c;
				List<Edge> edges = node[e.to];
				for(int i=-Collections.binarySearch(edges, left)-1;
						i<edges.size() && edges.get(i).c==e.c;
						i++){
					Edge ne = edges.get(i);
					if(used[ne.id] || used_v[ne.to]==vi) continue;
					used[ne.id] = true;
					used_v[ne.to] = vi;
					qu.add(ne);
				}
			}
		}
//		for(TreeSet<Integer> v: graph) System.out.println(v);
		System.out.println(bfs(graph, 0, n-1));
	}
	
	void edge(List<TreeSet<Integer>> graph, int u, int v){
		graph.get(u).add(v);
		graph.get(v).add(u);
	}
	
	int bfs(List<TreeSet<Integer>> graph, int s, int t){
		Queue<Integer> qu = new ArrayDeque<>();
		final int n = graph.size();
		int[] dist = new int[n];
		final int inf = Integer.MAX_VALUE/2;
		Arrays.fill(dist, inf);
		qu.add(s);
		dist[s] = 0;
		while(!qu.isEmpty()){
			int p = qu.poll();
			if(p==t) break;
			for(int v: graph.get(p)){
				if(dist[v]<=dist[p]) continue;
				dist[v] = dist[p]+1;
				qu.add(v);
			}
		}
		return dist[t]==inf?-1:dist[t]/2;
	}
	
	class Edge implements Comparable<Edge>{
		int to, c, id;
		Edge(int to, int c, int id){
			this.to = to;
			this.c = c;
			this.id = id;
		}
		@Override
		public int compareTo(Edge o) {
			if(c!=o.c) return c-o.c;
			return to-o.to;
		}
	}
