import java.util.*;

public class Main {
    
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int K = sc.nextInt();
        String ACL = "ACL";
        StringBuilder sb = new StringBuilder();
        for (int i = 0; i < K; i++) {
            sb.append(ACL);
        }
        System.out.println(sb);
    }
}