    class P implements Comparable<P>{

        int a;
        int i;

        P(int a, int i) {
            this.a = a;
            this.i = i;
        }

        public int compareTo(P p) {
            return Integer.compare(Math.abs(a), Math.abs(p.a));
        }
    }

    class Pair implements Comparable<Pair>{

        int a;
        int b;
        int i;

        Pair(int a, int b, int i) {
            this.a = a;
            this.b = b;
            this.i = i;
        }

        public int compareTo(Pair p) {

            if (a > p.a) return -1;
            if (a < p.a) return 1;
            if (b > p.b) return 1;
            if (b < p.b) return -1;
            return Integer.compare(i, p.i);
        }
    }

