import java.util.Scanner;

class Main{
    public static void main(String[] args){
    	
    	Scanner sc = new Scanner(System.in);
    	
    	int Answer = 0;
    	String strnum = sc.nextLine();
    	String houkou = sc.nextLine();
    	
    	Dice dice = new Dice(strnum);
    	
    	for( int i = 0 ; i < houkou.length() ; i++ ){
    		
    		if( houkou.charAt(i) == 'E'){
    			Answer = dice.getE();
    		}
    		if( houkou.charAt(i) == 'N'){
    			Answer = dice.getN();
    		}
    		if( houkou.charAt(i) == 'S'){
    			Answer = dice.getS();
    		}
    		if( houkou.charAt(i) == 'W'){
    			Answer = dice.getW();
    		}
    	}
    	
    	System.out.println(Answer);
    	
    }
}

class Dice{
	
	int[] dice = new int[6];
	String[] me;
	int kari = 0;
	
	
	
	
	
	public Dice(String strnum){
		
		me = strnum.split(" ");
		
		for(int i = 0 ; i < me.length ; i++ ){
			dice[i] = Integer.parseInt(me[i]);
		}
		
	}
	
	
	
	
	
	public int getE(){
		kari = dice[0];
        dice[0] = dice[3] ;
        dice[3] = dice[5] ;
        dice[5] = dice[2];
        dice[2] = kari;
        return dice[0];
	}
	
	public int getN(){
		kari = dice[0];
        dice[0] = dice[1] ;
        dice[1] = dice[5] ;
        dice[5] = dice[4];
        dice[4] = kari;
        return dice[0];
	}
	public int getS(){
		 kari = dice[0];
	     dice[0] = dice[4] ;
	     dice[4] = dice[5] ;
	     dice[5] = dice[1];
	     dice[1] = kari;
	     return dice[0];
	}
	public int getW(){
		 kari = dice[0];
	     dice[0] = dice[2] ;
	     dice[2] = dice[5] ;
	     dice[5] = dice[3];
	     dice[3] = kari;
	     return dice[0];
	}
	
}