import java.io.*;
public class Main{
	static String[][] map;
	static int count;
	static int w, h;
	public static void main(String[] args){
		BufferedReader reader = new BufferedReader(new InputStreamReader(System.in));
		String line;
		String[] linearr;
		String[] linearr2;
		int i;
		int j;
		
		try{
			while(null != (line=reader.readLine())){
				count = 0;
				linearr = line.split(" ");
				w = Integer.parseInt(linearr[0]);
				h = Integer.parseInt(linearr[1]);
				if(w==0 && h==0){
					break;
				}
				//??¨????????????map????????????
				map = new String[h][w];
				for(i=0; i<h; i++){
					line = reader.readLine();
					linearr2 = line.split(" ");
					System.arraycopy(linearr2, 0, map[i], 0, w);
				}
				//map?????????????????§??¢?´¢??????
				for(i=0; i<h; i++){
					for(j=0; j<w; j++){
						search(i, j, true);
					}
				}
				System.out.println(count);
			}
		}catch(IOException e){
			System.out.println(e);
		}
		
		
	}
	public static void search(int ii, int jj){
		search(ii, jj, false);
	}
	public static void search(int ii, int jj, boolean f){
		//??¨?????¢???????????? or ???
		if(ii<0 || jj<0 || ii>=h || jj>=w
		|| map[ii][jj].equals("0") ){
			return;
		}
		//??¶
		if(f){
			count ++;
		}
		//??¢?´¢????????§???
		map[ii][jj] = "0";
		//???????????????count?????\?????????
		search(ii-1, jj-1);
		search(ii-1, jj);
		search(ii-1, jj+1);
		search(ii, jj-1);
		search(ii, jj+1);
		search(ii+1, jj-1);
		search(ii+1, jj);
		search(ii+1, jj+1);
		//??????????????¨?????£??????????????¨????????§???????????£???
	}
}