
import java.util.Scanner;

public class Main{
	public static void main(String[] args){
		Scanner in = new Scanner(System.in);
		int n;	//??????????????°?????¨????????°
		int k = 3;	//?????????????????????????????°
		int x;	//??????????????????????????°?????¨????????°
		int year;	//???????????´??°
		n = in.nextInt();
		
		//??????????????????????????°x???????????¨??¨???
		while(n != 0){
			x = 0;
			year = 0;
			
			if(n<=k){
				x = (int)Math.pow(2,n-1);
			}else{
				int[] aryX = new int[n];	//??????????????????????????????????????°???????´?????????????
				for(int i=0; i<k; i++){
					aryX[i] = (int)Math.pow(2, i);
				}
				for(int i=k; i<n; i++){
					for(int j=0; j<k; j++){
						aryX[i] += aryX[i-j-1];
					}
				}
				x = aryX[n-1];
			}
			
			if(x%3650>0){
				year = x/3650 + 1;
			}else{
				year = x/3650;
			}
			
			System.out.println(year);
			n = in.nextInt();
		}
		System.out.println();
		in.close();
	}
}