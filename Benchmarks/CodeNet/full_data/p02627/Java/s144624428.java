import java.util.*;
import java.util.function.*;
import java.lang.*;
import java.io.*;


public class Main {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        char a = sc.next().charAt(0);
        if('a' <= a && a <= 'z') System.out.println("a");
        if('A' <= a && a <= 'Z') System.out.println("A");
        
    }
}
