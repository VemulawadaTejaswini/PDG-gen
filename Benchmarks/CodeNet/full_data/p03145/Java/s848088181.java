import java.util.*;

class Main{
  public static void main(String[] args){
    Scanner sc = new Scanner(system.in);
    int takasa = 0;
    int teihen = 0;
    int syahen = 0;
    takasa = sc.nextInt();
    teihen = sc.nextInt();
    syahen = sc.nextInt();
    syahen = 0.5;
      
    int menseki = 0;
    menseki = (teihen * takasa) * syahen;
    System.out.println(menseki);
  }
}