import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;
import java.util.ArrayDeque;

public class Main {
	public static final int value = 0;
	public static final int weight = 1;
	public static final int pointer = 2;

	public static void main(String[] args) throws NumberFormatException, IOException {
		// TODO ?????????????????????????????????????????????
		BufferedReader br =
				new BufferedReader(new InputStreamReader(System.in));
		for (int c = 1; ; c++) {
			int W = Integer.parseInt(br.readLine());
			if (W == 0) break;

			int N = Integer.parseInt(br.readLine());
			int[][] vw = new int[2][N];
			for (int i=0; i < N; i++) {
				String[] temp = br.readLine().split(",");
				vw[value][i] = Integer.parseInt(temp[0]);
				vw[weight][i] = Integer.parseInt(temp[1]);
			}

			int[] ans = minimax(vw, W, N);
			System.out.println("Case " + c + ":");
			System.out.println(ans[0]);
			System.out.println(ans[1]);
		}
	}

	public static int[] minimax(int[][] vw, int W, int N) {
		ArrayDeque<int[]> queue = new ArrayDeque<>();
		int[] ret = new int[2]; //
		/*
		 * loot[value] : ?¢¨???????????\??????????????????????????????
		 * loot[weight] : ?¢¨???????????\??????????????????????????????
		 * loot[pointer] : ?????????????????????pointer?????????????????§???
		 * ?¢¨???????????\???????????\????????????????¨?????????¨??????????????¨???index
		 */
		int[] loot = new int[] {0, 0, 0}; // value=0, weight=0, pointer=0
		queue.offer(loot);
		while ( ! queue.isEmpty() ) {
			loot = queue.poll();
			if (loot[pointer] == N) {
				if (loot[value] > ret[value]) {
					ret[value] = loot[value];
					ret[weight] = loot[weight];
				} else if (loot[value] == ret[value]) {
					ret[weight] = Math.min(ret[weight], loot[weight]);
				}
				continue;
			}

			if (vw[weight][ loot[pointer] ] + loot[weight] > W) {
				loot[pointer]++;
				queue.offer(loot);
				continue;
			}
			/*
			 * lootplus : loot[pointer]???????????????????¢¨???????????\????????´????????¶???
			 */
			int[] lootplus = new int[3];
			lootplus[value] = loot[value] + vw[value][ loot[pointer] ];
			lootplus[weight] = loot[weight] + vw[weight][ loot[pointer] ];
			lootplus[pointer] = loot[pointer] + 1;

			loot[pointer]++;

			queue.offer(loot);
			queue.offer(lootplus);
		}
		return ret;
	}
}