public class Main {
    public static void main(String[] args) {


        Scanner sc=new Scanner(System.in);
        while(true){//???????????¨????????????
            String line=sc.nextLine();

            if(line.equals("0")){
                break;
            }


            int judgeNum=Integer.parseInt(line);
            int[] point=new int[judgeNum];//?????????????????£?????????????????????

            for(int i=0; i<judgeNum; i++){//?????£???????????????????????????????????????????????§?????°???
                String line2=sc.nextLine();
                point[i]=Integer.parseInt(line2);
            }


            int max=point[0]; int min=point[0];
            for(int i=1; i<judgeNum; i++){//max,min????´¢
                if(point[i]>max){
                    max=point[i];
                }
                if(point[i]<min){
                    min=point[i];
                }
            }
            int pointSum=point[0];//????´?????¨?
            for(int i=1; i<point.length; i++){
                pointSum+=point[i];
            }
            int endPoint=(pointSum-max-min)/(judgeNum-2);
            System.out.println(endPoint);


        }
    }
}