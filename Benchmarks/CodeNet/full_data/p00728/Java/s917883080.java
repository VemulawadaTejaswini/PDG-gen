public class Main{
	public static void main(String[] args){
		int i,j,k,sum=0,average=0,max_index,min_index;
	
		for(i=0; i<args.length; i = i+Integer.parseInt(args[i])+1){
			sum = 0;
			//????????§max,min???????????????
			max_index = i+1;
			min_index = i+1;
			for(k=0; k<Integer.parseInt(args[i])-1; k++){
				if(Integer.parseInt(args[i+k+1]) < Integer.parseInt(args[i+k+2])){
					max_index = i+k+2;
				}else if(Integer.parseInt(args[i+k+1]) > Integer.parseInt(args[i+k+2])){
					min_index = i+k+2;
				}
			}
			for(j=0; j<Integer.parseInt(args[i]); j++){
				//????????§max,min????????????????¨???????????????????
				if(i+j+1 != max_index && i+j+1 != min_index){
					sum += Integer.parseInt(args[i+j+1]);
				}
			}
			//????????§??????????????£??????????????????
			average = sum/(Integer.parseInt(args[i])-2);
			System.out.println(average);
		}
	}
}