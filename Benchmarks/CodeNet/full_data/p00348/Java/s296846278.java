import java.io.*;
import java.util.ArrayList;
import java.util.List;

class Main {
	public static void main(String[] args) {
		try {
			BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
			List<Integer> listForSort = new ArrayList<>();// ????´????????´????????????????
			int sortCounter = 0;

			int elementsNum = Integer.parseInt(br.readLine()); // ??±???????´???°
			String line = br.readLine(); // ????????????????´???????????´?
			String[] dataSet = line.split(" ");

			for (int i = 0; i < elementsNum; i++) {
				listForSort.add(i, Integer.parseInt(dataSet[i]));
			}
			
			int index = 0;
			while (index < listForSort.size()) {
				if (index==0 || !compare(listForSort, index)) {
					++index;
				} else {
					listForSort = executeSort(listForSort, index);
					++sortCounter;
					--index;
				}
			}
			System.out.println(sortCounter);
		} catch (Exception e) {
			e.printStackTrace();
		}
	}

	/* ?????????????´???¨????????´??????????´?????????????
	 * param ????´????????´???????????????????
	 * param ?????????????´????index
	 * return ?????????????´??????????????????´??????????´???????????°????????????°false
	 */
	private static boolean compare(List<Integer> list, int index) {
		if (list.get(index) < list.get(index - 1)) {
			return true;
		}
		return false;
	}

	/* ??´??????????´????????°?????§??????????
	 * param ????´????????´???????????????????
	 * param ?????????????´????index
	 * return ????????????????????????
	 */
	private static List<Integer> executeSort(List<Integer> list, int index) {
		int tmp = list.get(index-1);
		list.remove(index-1);
		list.add(tmp);
		return list;
	}
}