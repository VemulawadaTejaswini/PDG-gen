import java.util.*;

class Rectangle {
    public static void main(String...args) {
        final Scanner sc = new Scanner(System.in);
        final int a = sc.nextInt();
        final int b = sc.nextInt();
        System.out.printf("%d %d\n", a * b, (a + b) * 2);
    }
}
public class Main {
    public static void main(String... args) {
        Rectangle.main();
    }
}