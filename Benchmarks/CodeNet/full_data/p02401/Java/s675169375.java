import java.util.*;

class Main {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        
        while(true) {
            int a = scan.nextInt();
            String op = scan.next();
            int b = scan.next.Int();

            if(op == "?") break;
            
            if(op == "+" ) 
                  System.out.println(a+b);
            else if (op == "-")
                  System.out.println(a-b);
            else if (op == "*")
                  System.out.println(a*b);
            else if (op == "/")
                  System.out.println(a/b);
            else;
        }
     }
}