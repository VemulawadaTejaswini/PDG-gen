import java.io.*;
import java.util.*;

class Main{
public static void main(string[] args){
  Scanner scan = new Scanner(System.in);

  char op = scan.next.charAT();
  int a = scan.nextInt();
  int b = scan.nextInt();

  if (op == "+"){
    System.out.println(a + b);
  }

  if (op == "-"){
    System.out.println(a - b);
  }

  if (op == "*"){
    System.out.println(a * b);
  }

  if (op == "/"){
    System.out.println(a / b);
  }

}
}