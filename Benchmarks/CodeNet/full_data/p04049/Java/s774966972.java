    public static class Task implements Runnable {
        final FastIO io;
        final Debug debug;
        int inf = (int) 1e8;
        long lInf = (long) 1e18;
        double dInf = 1e50;

        public Task(FastIO io, Debug debug) {
            this.io = io;
            this.debug = debug;
        }

        @Override
        public void run() {
            solve();
        }

        int[][] dist;

        public void solve() {
            long now = System.nanoTime();
            int n = io.readInt();
            int k = io.readInt();
            Node[] nodes = new Node[n + 1];
            Edge[] edges = new Edge[n - 1];
            for (int i = 1; i <= n; i++) {
                nodes[i] = new Node();
                nodes[i].id = i;
            }
            for (int i = 1; i < n; i++) {
                Node a = nodes[io.readInt()];
                Node b = nodes[io.readInt()];
                a.next.add(b);
                b.next.add(a);
                edges[i - 1] = new Edge();
                edges[i - 1].a = a;
                edges[i - 1].b = b;
            }
            dist = new int[n + 1][n + 1];
            for (int i = 1; i <= n; i++) {
                dfsForDepth(nodes[i], null, 0);
                for (int j = 1; j <= n; j++) {
                    dist[i][j] = nodes[j].depth;
                }
            }


            int max = 0;
            int[] id1 = new int[2];
            int[] id2 = new int[2];
            while (System.nanoTime() - now < 1800000000) {
                Randomized.randomizedArray(edges, 0, n - 1);
                for (int i = 1; i <= n; i++) {
                    nodes[i].init();
                }
                for (Edge e : edges) {
                    Node a = e.a.find();
                    Node b = e.b.find();

                    id1[0] = a.a;
                    id1[1] = a.b;
                    id2[0] = b.a;
                    id2[1] = b.b;

                    int f1 = id1[0];
                    int f2 = id2[0];

                    for (int i = 0; i < 2; i++) {
                        for (int j = 0; j < 2; j++) {
                            if (dist[id1[i]][id2[j]] > dist[f1][f2]) {
                                f1 = id1[i];
                                f2 = id2[j];
                            }
                        }
                    }

                    if (dist[f1][f2] > k) {
                        continue;
                    }

                    Node.merge(a, b);
                    a.find().a = f1;
                    a.find().b = f2;
                    max = Math.max(max, a.find().size);
                }
            }

            io.cache.append(n - max);
        }

        private void dfsForDepth(Node root, Node p, int d) {
            root.depth = d;
            for (Node node : root.next) {
                if (node == p) {
                    continue;
                }
                dfsForDepth(node, root, d + 1);
            }
        }
    }

