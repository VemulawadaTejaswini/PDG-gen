    void solve(){
        int H = in.nextInt(), W = in.nextInt(), K = in.nextInt();
        int sy = in.nextInt()-1, sx = in.nextInt()-1, gy = in.nextInt()-1, gx = in.nextInt()-1;
        char[][] c = new char[H][W];
        for(int i = 0; i < H; i++){
            for(int j = 0; j < W; j++){
                c[i][j] = in.nextChar();
            }
        }
        
        int INF = Integer.MAX_VALUE / 2;
        int[][] dist = new int[H][W];
        for(int i = 0; i < H; i++) Arrays.fill(dist[i], INF);
        ArrayDeque<int[]> queue = new ArrayDeque<int[]>();
        queue.addLast(new int[]{sy, sx});
        dist[sy][sx] = 0;
    
        //4近傍
        int[] dy = {-1, 0, 1, 0};
        int[] dx = {0, 1, 0, -1};
    
        while(!queue.isEmpty()){
            int[] q = queue.removeFirst();
            int y = q[0], x = q[1], m = dist[y][x];
            LOOP:for(int k = 0; k < dy.length; k++){
                for(int d = 1; d <= K ; d++){
                    int y2 = y + dy[k] * d;
                    int x2 = x + dx[k] * d;
                    if(y2 < 0 || y2 >= H || x2 < 0 || x2 >= W) continue LOOP;
                    if(c[y2][x2] == '@') continue LOOP;
                    
                    //https://twitter.com/chokudai/status/1272168231172632576?s=20
                    //https://atcoder.jp/contests/abc170/submissions/14358430
                    if(dist[y2][x2] <= m) continue LOOP;
                    if(dist[y2][x2] > m + 1){
                        dist[y2][x2] = m + 1;
                        queue.addLast(new int[]{y2, x2});
                    }
                }
            }
        }
        out.println(dist[gy][gx] == INF ? "-1" : dist[gy][gx]);
    }
    
