import java.util.Scanner;
public class Main{
    public static void main(String[] args){
        Scanner scanner = new Scanner(System.in);

        int N = scanner.nextInt();
        int A = scanner.nextInt();
        int B = scanner.nextInt();
        if((A + B = 0)){
            int result = 0;
        }
        result = N % (A + B);
        if(A == 0 || (A + B) == 0){
            int i = 0;
        }
        i = N / (A + B) * A;

        i = result + i;

        System.out.println(i);
    }
}