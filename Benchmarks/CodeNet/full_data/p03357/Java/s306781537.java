import java.io.*;
import java.util.*;

public class Main {
  private static int solve(int n, char[] cs, int[] as) {
    int n2 = 2 * n;
    int[] cNumbers = new int[n2];
    for (int i = 0; i < n2; i++) {
      cNumbers[i] = cs[i] == 'W' ? 1 : 0;
    }

    int[][] map = new int[2][n + 1];
    for (int i = 0; i < n2; i++) {
      map[cNumbers[i]][as[i]] = i;
    }

    int[][][] countRightTable = new int[2][n + 1][n2];
    for (int c = 0; c < 2; c++) {
      for (int number = 1; number <= n; number++) {
        for (int j = 0; j <= map[c][number]; j++) {
          countRightTable[c][number][j] = countRightTable[c][number - 1][j] + 1;
        }
        for (int j = map[c][number] + 1; j < n2; j++) {
          countRightTable[c][number][j] = countRightTable[c][number - 1][j];
        }
      }
    }

    int[][] dp = new int[n + 1][n + 1];
    for (int i = 0; i <= n; i++) {
      for (int j = 0; j <= n; j++) {
        dp[i][j] = Integer.MAX_VALUE;
      }
    }
    dp[0][0] = 0;

    for (int i = 0; i <= n; i++) {
      for (int j = 0; j <= n; j++) {
        if (i + 1 <= n) {
          int index = map[0][i + 1];
          int shiftedIndex = index + countRightTable[0][i][index] + countRightTable[1][j][index];
          dp[i + 1][j] = Math.min(dp[i + 1][j], dp[i][j] + shiftedIndex - i  - j);
        }
        if (j + 1 <= n) {
          int index = map[1][j + 1];
          int shiftedIndex = index + countRightTable[0][i][index] + countRightTable[1][j][index];
          dp[i][j + 1] = Math.min(dp[i][j + 1], dp[i][j] + shiftedIndex - i - j);
        }
      }
    }

    return dp[n][n];
  }

  private static void execute(ContestReader reader, ContestWriter out) {
    int n = reader.nextInt();
    char[] cs = new char[2 * n];
    int[] as = new int[2 * n];
    for (int i = 0; i < 2 * n; i++) {
      cs[i] = reader.nextCharArray()[0];
      as[i] = reader.nextInt();
    }
    out.println(solve(n, cs, as));
  }
  
  public static void main(String[] args) {
    ContestReader reader = new ContestReader(System.in);
    ContestWriter out = new ContestWriter(System.out);
    execute(reader, out);
    out.flush();
  }
}

