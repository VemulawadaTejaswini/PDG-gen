public class Main{
	public static void main (String args[]){
		Scanner sc = new Scanner(System.in);

		while(true){
			int x = sc.nextInt();
			int n = sc.nextInt();
			int count = 0;

			if(x ==0 && n ==0){
				break;
			}else{

				for(int i = 0; i < n-2; i++){
					for(int j = i + 1; j < n-1; j++){
						for(int k = j + 1; k < n; k++){

							if(x == (i + j + k)){
								count++;
							}
						}
					}
				}
			}
			System.out.println(count);
		}
		sc.close();
	}
}