	//x以下の素数を列挙する
	static void cieve(int x) {
		prime = new int[x+1];
		Arrays.fill(prime, -1);
		prime[0] = prime[1] = 0;
		for(int i=2;i<x+1;i++) {
			if(prime[i]!=-1)continue;
			for(int j=2*i;j<x+1;j+=i) {
				prime[j] = i;
			}
		}
	}
	

	//MOD culculations
