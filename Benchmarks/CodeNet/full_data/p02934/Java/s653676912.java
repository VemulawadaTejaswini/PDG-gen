import java.util.Scanner;

public class Main {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int N = sc.nextInt();
		int A[] = new int[N];
		for(int i = 0; i < N; i++) {
			A[i] = sc.nextInt();
		}
		double sum = 0;
		for(int i = 0; i < N; i++) {
			sum += (double)1 / A[i];
		}
		System.out.println(1 / sum);
		sc.close();
	}

}
