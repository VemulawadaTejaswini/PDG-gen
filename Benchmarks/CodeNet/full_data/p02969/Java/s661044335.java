import java.io.PrintWriter;
import java.util.*;

public class Main {
		public static void main(String[] args) {
			Scanner stdin = new Scanner(System.in);
			PrintWriter out = new PrintWriter(System.out);
			
			int r = Integer.parseInt(stdin.next());
			
			out.println(3*r*r);
			
			out.flush();
	}
}


