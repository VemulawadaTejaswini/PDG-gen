import java.util.*;
 
public class Main {
    public static void main(String[] args) throws Exception {
        Scanner sc = new Scanner(System.in);
        
        //int customers = Integer.parseInt(sc.next());
        String s = sc.next();
        if(s.equals("hi")) System.out.println("Yes");
        else if(s.equals("hihi")) System.out.println("Yes");
        else if(s.equals("hihihi")) System.out.println("Yes");
        else if(s.equals("hihihihi")) System.out.println("Yes");
        else if(s.equals("hihihihihi")) System.out.println("Yes");
        else System.out.println("No");
        
    }
    
}