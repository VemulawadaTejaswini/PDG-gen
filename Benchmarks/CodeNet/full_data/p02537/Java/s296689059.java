import java.io.InputStream;
import java.util.Arrays;
import java.util.function.IntUnaryOperator;
import java.util.function.LongUnaryOperator;

public class Main {
    public static void main(String[] args) throws Exception {
        ExtendedScanner sc = new ExtendedScanner();
        FastPrintStream pw = new FastPrintStream();
        solve(sc, pw);
        sc.close();
        pw.flush();
        pw.close();
    }

    public static void solve(ExtendedScanner sc, FastPrintStream pw) {
        int n = sc.nextInt();
        int k = sc.nextInt();
        int[] a = sc.ints(n);
        IntSegmentTree t = new IntSegmentTree(300001, Integer::max, 0);
        for (int i = 0; i < n; i++) {
            int l = Math.max(a[i] - k, 0);
            int r = Math.min(a[i] + k + 1, 300001);
            int len = t.prod(l, r) + 1;
            t.set(a[i], len);
        }
        pw.println(t.allProd());
    }
}

