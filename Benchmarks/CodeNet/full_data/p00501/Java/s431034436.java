import java.util.*;
public class Main {
	public static void main(String[] args){
		Scanner sc = new Scanner(System.in);
		int n = sc.nextInt();
		int space,sum = 0,cnt;
		char[][] str = new char[n + 1][];
		for(int i = 0; i < n + 1; i++){
				str[i] = sc.next().toCharArray();
		}
		for(int i = 1; i < str.length; i++){
			space = 0;
			//System.out.println(i + " ????????????????????????");
			out:for(int k = 0; k <= str[i].length; k++){
				if(str[i][k] == str[0][0]){
					//System.out.println("1 ?????? "+i+" ??? "+(k + 1)+" ?????????????????? "+str[i][k]+" ?????????????????????");					
					for(int j = k; j <= str[i].length; j++){
						if(str[i][j] == str[0][1]){
							//System.out.println("2 ?????? "+i+" ??? "+(j + 1)+" ?????????????????? "+str[i][j]+" ?????????????????????");
							space = j - k;
							//System.out.println("?????????????????? "+ space +" ??ยง???");
							cnt = 2;
							if((j + space + 1) <= str[i].length){
								for(int l = j + space; l < str[i].length; l += space){
									if(str[i][l] == str[0][cnt]){
										//System.out.println("3 ?????? "+i+" ??? "+(l + 1)+" ?????????????????? "+str[i][l]+" ?????????????????????");
										cnt++;
										if(cnt == str[0].length){
											//System.out.println(i + " ??????????????ยง????????????????????????");
											sum++;
											break out;									
										}
									}
									else break;
								}
							}
							else break out;
						}
					}
				}
			}
		}
		System.out.println(sum);
	}
}