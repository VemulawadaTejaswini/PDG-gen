	private static void solve() {
		int n = nei();
		int m = nei();
		int k = nei();
		int[] ds = nis(m);
		int[][] vs = nis2(n, k);
		for (int i = 0; i < m; i++) {
			ds[i]--;
		}
		for (int i = 0; i < n; i++) {
			for (int j = 0; j < k; j++) {
				vs[i][j]--;
			}
		}
		int[] idxToDark = new int[n];
		for (int i = 0; i < n; i++) {
			idxToDark[i] = -1;
		}
		for (int i = 0; i < m; i++) {
			idxToDark[ds[i]] = i;
		}
		int MAX_BIT = 1 << m;
		int[][] edges = new int[MAX_BIT][k];
		for (int i = 0; i < MAX_BIT; i++) {
			for (int j = 0; j < k; j++) {
				int to = 0;
				for (int l = 0; l < m; l++) {
					if ((1 << l & i) != 0) {
						int darkIdx = ds[l];
						int toDarkIdx = idxToDark[vs[darkIdx][j]];
						if (toDarkIdx != -1) {
							to |= 1 << toDarkIdx;
						}
					}
				}
				edges[i][j] = to;
			}
		}

		Queue<int[]> q = new LinkedList<int[]>();
		q.add(new int[] { MAX_BIT - 1, 0 });
		boolean[] visited = new boolean[MAX_BIT];
		while (!q.isEmpty()) {
			int[] current = q.poll();
			int idx = current[0];
			if (visited[idx])
				continue;
			visited[idx] = true;
			int cost = current[1];
			if (idx == 0)
				kil(cost);

			for (int i = 0; i < k; i++) {
				q.add(new int[] { edges[idx][i], cost + 1 });
			}
		}
	}

