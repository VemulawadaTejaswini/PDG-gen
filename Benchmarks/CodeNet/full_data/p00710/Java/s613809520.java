import java.util.Scanner;

public class Main {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);

		while (true) {
			// ?????????????????°???n?????£??\
			int n = sc.nextInt();
			// ?????£????????????????????°???r?????£??\
			int r = sc.nextInt();
			if (n == 0 && r == 0)
				break;

			// ??????????????????????????????????´?????????¢??°deck
			int[] deck = scanCard(n);

			for (int i = 0; i < r; i++) {
				// ?????????????????????????????????
				int p = sc.nextInt();
				// p????????????c????????????????????????
				int c = sc.nextInt();

				// ?????£????????????????????¢??°
				// p???????????????????????????c???p???????????????
				deck = shuffle(preserve(1, p - 1, deck), preserve(p, c, deck), deck);
			}
			System.out.println(deck[0]);
		}
	}

	public static int[] scanCard(int n) {
		int[] card = new int[n];
		for (int i = 0; i < card.length; i++) {
			card[i] = card.length - i;
		}
		return card;
	}

	public static int[] preserve(int firstTerm, int termNum, int[] array) {
		int[] newArray = new int[termNum];
		for (int i = 0; i < termNum; i++) {
			newArray[i] = array[firstTerm - 1 + i];
		}
		return newArray;
	}

	public static int[] shuffle(int[] preArray, int[] postArray, int[] array) {
		for (int i = 0; i < postArray.length; i++) {
			array[i] = postArray[i];
		}
		for (int i = 0; i < preArray.length; i++) {
			array[postArray.length + i] = preArray[i];
		}
		return array;
	}
}