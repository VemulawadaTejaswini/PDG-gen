import java.util.Scanner;

public class Main {
    private static void cut(int[] deck, int p, int c) {
        // deck???p????????????c????????????????????Â£????????????
        int[] temp = new int[c]; //p???????????????c????????????????????????
        System.arraycopy(deck, p-1, temp, 0, c); //p????????????c??????temp?????????
        System.arraycopy(deck, 0, deck, c, p-1); //p????????????c??????temp?????????
        System.arraycopy(temp, 0, deck, 0, c); //p????????????c??????temp?????????
   }

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n, r, p, c;

        while (true) {
            n = sc.nextInt();
            r = sc.nextInt();
            if (n == 0)
                break;

            int[] deck = new int[n];
            for (int i = 0; i < n; i++) {
                deck[i] = n-i;
            }

            for (int i = 0; i < r; i++) {
                p = sc.nextInt();
                c = sc.nextInt();
                cut(deck, p, c);
            }

            System.out.println(deck[0]);
        }

    }
}