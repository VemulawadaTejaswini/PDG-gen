public class Main {
    public static void main(String[] args) throws Exception {
        var sc = new FastScanner();
        var pw = new FastPrintStream();
        solve(sc, pw);
        sc.close();
        pw.flush();
        pw.close();
    }

    public static void solve(FastScanner sc, FastPrintStream pw) {
        final int N = sc.nextInt();
        final int Q = sc.nextInt();
        final var A = new Integer[N];
        java.util.Arrays.setAll(A, i -> sc.nextInt());
        var t = new SegTree<Integer>(A, Integer::max, -1);
        for (int $ = 0; $ < Q; $++) {
            int query = sc.nextInt();
            if (query == 1) {
                int x = sc.nextInt() - 1;
                int v = sc.nextInt();
                t.set(x, v);
            } else if (query == 2) {
                int l = sc.nextInt() - 1;
                int r = sc.nextInt();
                pw.println(t.prod(l, r));
            } else {
                int x = sc.nextInt() - 1;
                int v = sc.nextInt();
                pw.println(t.maxRight(a, e -> e < v) + 1);
            }
        }
    }
}

