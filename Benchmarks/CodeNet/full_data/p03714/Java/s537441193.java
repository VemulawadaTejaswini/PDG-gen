  public static class Solver {
    public Solver() {

      int N = sc.nextInt();

      long[] A = sc.nextLongArray(3*N, false);

      long sumL = 0;
      PriorityQueue<Long> left = new PriorityQueue<Long>();
      for (int i=0; i<N; i++) {
        left.add(A[i]);
        sumL += A[i];
      }

      long sumR = 0;
      boolean[] selected = new boolean[3*N];
      PriorityQueue<Integer> unselected = new PriorityQueue<Integer>((o1, o2) -> Compare.L(A[o1], A[o2]));
      for (int i=N; i<3*N; i++) {
        unselected.add(i);
      }

      for (int i=0; i<N; i++) {
        int j = unselected.remove();
        selected[j] = true;
        sumR += A[j];
      }

      long ans = sumL - sumR;
      for (int b=N; b<2*N; b++) {
        if (A[b] > left.peek()) {
          sumL -= left.remove();
          left.add(A[b]);
          sumL += A[b];
        }
        if (selected[b]) {
          selected[b] = false;
          sumR -= A[b];
          int i;
          while ((i = unselected.remove()) <= b);
          selected[i] = true;
          sumR += A[i];
        }
        ans = Math.max(ans, sumL - sumR);
      }

      out.println(ans);

    }
  }

  public static class Compare {
    public static int L(long l1, long l2) {
      if (l1 == l2) return 0;
      else if (l1 < l2) return -1;
      else return 1;
    }
    public static int P(Point p1, Point p2) {
      if (p1.x == p2.x) return p1.y - p2.y;
      else return p1.x - p2.x;
    }
  }

