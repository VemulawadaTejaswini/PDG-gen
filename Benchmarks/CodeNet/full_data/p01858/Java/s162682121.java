

import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;
import java.util.ArrayList;

public class Main{

	/*
	 * ????????°???????????¨?????????????????????
	 */
	public static void main(String[] args) throws IOException {

		BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
		// ??????????????°
		int set = Integer.parseInt(br.readLine());
		// ?????¬?????????????????????????????????
		Player isono = new Player("Isono-kun");
		Player nakajima = new Player("Nakajima-kun");

		// ??????????????????????????????????????????????´?
		for (int cnt = 0; cnt < set; cnt++)
			isono.getGesture().add(Action.valueOf(br.readLine()));
		for (int cnt = 0; cnt < set; cnt++)
			nakajima.getGesture().add(Action.valueOf(br.readLine()));

		// ????????????????????????????????????
		for (int turn = 0; turn < set; turn++) {
			// ???????????\????????????2???????????????
			for (int swap = 0; swap < 2; swap++) {
				Player p1 = swap == 0 ? isono : nakajima;
				Player p2 = swap == 0 ? nakajima : isono;

				switch (p2.getGesture().get(turn)) {
				case kougekida:
					// p2???????????????
					if (p2.getForce() == 0 && !(p1.getGesture().get(turn) == Action.kougekida)) {
						System.out.println(p1.getPlayerName());
						return;
					}
					// ????????¨??????????????¢?????????
					if (p1.getGesture().get(turn) == Action.kougekida) {
						if (p1.getForce() > p2.getForce()) {
							System.out.println(p1.getPlayerName());
							return;
						}
					}
					break;

				case tameru:
					// ????????§???????????´????????????
					if (p1.getGesture().get(turn) == Action.kougekida && p1.getForce() > 0) {
						System.out.println(p1.getPlayerName());
						return;
					}
					break;

				case mamoru:
					// ???????????????????????\????????????????????????
					if (p1.getGesture().get(turn) == Action.kougekida && p1.getForce() >= 5) {
						System.out.println(p1.getPlayerName());
						return;
					}
					break;
				}
			}
			// ???????????¢?????¨??£?????´??????????????????0?????????
			if (isono.getGesture().get(turn) == Action.kougekida)isono.setForce(0);
			if (nakajima.getGesture().get(turn) == Action.kougekida)nakajima.setForce(0);
			// ?????????
			if (isono.getGesture().get(turn) == Action.tameru)isono.setForce(isono.getForce() + 1);
			if (nakajima.getGesture().get(turn) == Action.tameru)nakajima.setForce(nakajima.getForce() + 1);
		}
		System.out.println("Hikiwake-kun");
	}// ?????????????????????????????????
}// ??????????????????????????????

enum Action {
	kougekida, tameru, mamoru
}// Action?????????????????????

/**
 * ?????¬??????????????¶???
 *
 * @author ?????????????????§???
 */
class Player {

	private int force = 0;
	private String PlayerName;
	private ArrayList<Action> gesture = new ArrayList<Action>();

	public ArrayList<Action> getGesture() {
		return gesture;
	}

	public String getPlayerName() {
		return PlayerName;
	}

	public int getForce() {
		return force;
	}

	public void setForce(int force) {
		this.force = force;
	}

	Player(String name) {
		this.PlayerName = name;
	}
}// Player?????????????????????