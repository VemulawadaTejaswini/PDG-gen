import java.io.InputStream;
import java.util.Arrays;
import java.util.function.IntUnaryOperator;
import java.util.function.LongUnaryOperator;


public class Main {
    public static void main(String[] args) throws Exception {
        ExtendedScanner sc = new ExtendedScanner();
        FastPrintStream pw = new FastPrintStream();
        solve(sc, pw);
        sc.close();
        pw.flush();
        pw.close();
    }

    public static void solve(ExtendedScanner sc, FastPrintStream pw) {
        long n = sc.nextLong();
        int x = sc.nextInt();
        int m = sc.nextInt();
        if (m == 1) {
            pw.println(0);
            return;
        }
        int[] b = new int[m];
        Arrays.fill(b, -1);
        b[x] = 0;
        long[] s = new long[m + 1];
        long[] a = new long[m];
        a[0] = x;
        s[1] = x;
        for (int i = 1;; i++) {
            if (i == n) {
                pw.println(s[i]);
                return;
            }
            a[i] = (a[i - 1] * a[i - 1]) % m;
            int pre = b[(int) a[i]];
            if (pre >= 0) {
                long md = a[i];
                long ls = s[i] - s[pre];
                long ans = ls + s[pre];
                int loop = i - pre;
                ans += ((n - 1 - i) / loop) * ls;
                long idx = i + loop * ((n - 1 - i) / loop);
                while (idx < n) {
                    ans += md;
                    md = (md * md) % m;
                    idx++;
                }
                pw.println(ans);
                return;
            } else {
                b[(int) a[i]] = i;
                s[i + 1] = s[i] + a[i];
            }
        }
    }
}

