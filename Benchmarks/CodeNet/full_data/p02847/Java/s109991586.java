import java.util.*;

public class Main {
	public static void main (String[] args) {
		Scanner sc = new Scanner(System.in);
		String Nam = sc.next();
		
		if(Nam.equals("SUN")){System.out.println(7);}
		if(Nam.equals("MON")){System.out.println(6);}
		if(Nam.equals("TUE")){System.out.println(5);}
		if(Nam.equals("WED")){System.out.println(4);}
		if(Nam.equals("THU")){System.out.println(3);}
		if(Nam.equals("FRI")){System.out.println(2);}
		if(Nam.equals("SAT")){System.out.println(1);}
    }
}
