
import java.io.*;
import java.util.TreeMap;
import java.util.Map;

class Main {
	private static final String SEPARATOR = ",";
	
    public static void main(String[] args) {
        try {
            BufferedReader br = new BufferedReader(new InputStreamReader(System.in));// ??\????????????????????????
            int size = Integer.parseInt(br.readLine());
            Map<Integer, String> resultOfLots = new TreeMap<>();
            initialize(resultOfLots, size);
            
            int times = Integer.parseInt(br.readLine());
            String process = br.readLine();
            for (int i = 0; i < times; i++) {
            	swap(process, resultOfLots);
            	process = br.readLine();
            }
            
            for (int key : resultOfLots.keySet()) {
            	System.out.println(resultOfLots.get(key));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    /**
     * Map???n?????????????´????"n"??¨?????????????????????????????????????????????  
     * @param map
     *            ??????????????????key????????????????????§???value????????¨??????????????¨???
     */
    private static void initialize(Map<Integer, String> map, int num) {
    	for (int i = 1; i <= num; i++) {
    		map.put(i, String.valueOf(i));
        }
    }

    /**
     * ??\?????????2????????´??°a,b??????????????????Map??????a??¨b?????\????????????
     * @param line
     *            2????????´??°?????\??? "a,b"
     * @param map
     *            ????????????????????????????????????key??§????????¨????????????value??§??\??£?????????
     */
    private static void swap(String line, Map<Integer, String> map) {
    	String firstNum = line.substring(0, line.indexOf(SEPARATOR));
    	String lastNum = line.substring(line.indexOf(SEPARATOR)+1);
    	
    	map.replace(Integer.valueOf(firstNum), lastNum);
    	map.replace(Integer.valueOf(lastNum), firstNum);
    }
}