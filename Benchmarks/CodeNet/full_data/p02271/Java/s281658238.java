import java.util.Scanner;

public class ExhaustiveSearch{
    public static void main(String[] args){

	//Scanner??????????????????????????????scan?????????
	Scanner scan = new Scanner(System.in);

	//??????A?????\???????´?????????°n???????????????
	int n = scan.nextInt();
	
	//??????A???????????????
	int[] A = new int[n];
	for(int i = 0; i < n; i++){
	    A[i] = scan.nextInt();
	}

	//??????M?????\???????´?????????°q???????????????
	int q = scan.nextInt();

	//??????M???????????????
	int[] M = new int[q];
	for(int i = 0; i < q; i++){
	    M[i] = scan.nextInt();
	}

	for(int i = 0; i < q; i++){
	    if(solve(0,M[i], n, A)) System.out.println("yes");
	    else System.out.println("no");
	}
			       

    }

    public static boolean solve(int i, int m, int n, int[] A){
	
	if(m == 0) return true;
	if(i >= n) return false;

	boolean ans = solve(i + 1, m, n, A) || solve(i + 1, m - A[i], n, A);
	return ans;
    }
}