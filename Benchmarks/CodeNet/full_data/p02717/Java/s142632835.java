import java.util.Scanner;

public class ABCSwap {
    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        int x = in.nextInt();
        int y = in.nextInt();
        int z = in.nextInt();
        in.close();
        System.out.println(z + " " + x + " " + y);
    }
}