import java.util.Scanner;

class Main {
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        int h = sc.nextInt();
        int w = sc.nextInt();
 
        while(h*w != 0){

            for(int i=0;i<h;i++){

                for(int j=0;j<w;j++){

                System.out.printf("#");
                }

            System.out.printf("\n");
            h = scan.nextInt();
            w = scan.nextInt();
        }
        }
    }
}

    