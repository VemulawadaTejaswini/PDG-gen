	void calc(int N) {								
		int cur;							
		for(int i=2; i <= N; i++) {			// ２～Ｎを個別に素因数分解する				
			cur = i;						
			for(int j=2; j*j <= i; j++) {						
				while(cur % j == 0) {					
					E[j]++;				
					cur = cur / j;				
				}					
			}						
			if(cur > 1)	E[cur]++;		// 素数			
		}							
	}								

