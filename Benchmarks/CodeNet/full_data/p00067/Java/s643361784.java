class IslandMapp{
	int[][] sima;
	int tate;
	int yoko;


	public IslandMapp(int i, int j, int[][] mapp) {
		this.tate = i;
		this.yoko = j;
		this.sima = mapp;
	}


	public static void check(int tate , int yoko, int[][] sima){

		sima[tate][yoko] = 0;

		if(tate+1 < 12){
			if(sima[tate+1][yoko] == 1){
				check(tate+1 , yoko , sima);
			}
		}
		if(yoko+1 < 12){
			if(sima[tate][yoko+1] == 1){
				check(tate , yoko+1 , sima);
			}
		}
		if(tate-1 > -1){
			if(sima[tate-1][yoko] == 1){
				check(tate-1 , yoko , sima);
			}
		}
		if(yoko-1 > -1){
			if(sima[tate][yoko-1] == 1){
				check(tate , yoko-1 , sima);
			}
		}
	}
}