package volume0;

import java.io.IOException;
import java.util.Scanner;

public class MAIN {

	// 10*10????????????????????????????????´???????????????14*14?????????????????????
	private static int[][] square = new int[14][14];
	private static int SMALL_INK = 1;
	private static int LARGE_INK = 3;
	private static int ALIGNMENT = 2;

	/**
	 * ?¨??????\??????????????????????????????????????????????????????????????£????????????????????????????????????
	 * ??????????????\??????????????????????????????????????????????????§????????????????????°??¨???????????????????????????????????????????????????
	 *
	 * @param args
	 * @throws IOException
	 */
	public static void main(String[] args) throws IOException {

		int mostDeep = 0;
		int whiteTiles = 0;

		String[] inputString;
		String readLine = "";

		Scanner input = new Scanner(System.in);

		while (input.hasNext()) {

			readLine = input.next();

			// ???????????£????????????????????????????????????
			inputString = readLine.split(",", 0);

			inkPoint(Integer.parseInt(inputString[0]) + ALIGNMENT,
					Integer.parseInt(inputString[1]) + ALIGNMENT,
					Integer.parseInt(inputString[2]));

		}

		// 10*10?????????????????°?????????????????????????????????????????°??¨???????????????????????????????????????
		for (int i = 2; i < 12; i++) {
			for (int j = 2; j < 12; j++) {
				if (square[i][j] == 0) {
					whiteTiles++;
				} else {
					if (square[i][j] > mostDeep) {
						mostDeep = square[i][j];
					}
				}
			}
		}
		System.out.println(whiteTiles + "\r\n" + mostDeep);
	}

	/**
	 * square???2???12??????????????????????????¨???
	 *
	 * @param x
	 * @param y
	 * @param inkSize
	 */
	static void inkPoint(int x, int y, int inkSize) {

		square[x][y - 1]++;
		square[x - 1][y]++;
		square[x][y]++;
		square[x + 1][y]++;
		square[x][y + 1]++;

		if (inkSize != SMALL_INK) {
			square[x - 1][y - 1]++;
			square[x + 1][y - 1]++;
			square[x - 1][y + 1]++;
			square[x + 1][y + 1]++;
		}

		if (inkSize == LARGE_INK) {
			square[x][y - 2]++;
			square[x - 2][y]++;
			square[x + 2][y]++;
			square[x][y + 2]++;
		}
	}
}