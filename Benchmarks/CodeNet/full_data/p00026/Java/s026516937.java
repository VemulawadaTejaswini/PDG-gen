import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;

public class Main {

	private static int[][] square = new int[10][10];
	private static int mostDeep = 0;
	private static int whiteTiles = 0;

	/**
	 * ?¨??????\??????????????????????????????????????????????????????????????£????????????????????????????????????
	 * ??????????????\??????????????????????????????????????????????????§????????????????????°??¨???????????????????????????????????????????????????
	 *
	 * @param args
	 * @throws IOException
	 */
	public static void main(String[] args) throws IOException {
		// TODO ?????????????????????????????????????????????

		String[] inputString;
		String readLine = "";

		BufferedReader input = new BufferedReader(new InputStreamReader(
				System.in));

		while (true) {

			readLine = input.readLine();

			// ??\????????????????????´??????????????????????????????????????????
			if (readLine.equals("")) {
				break;
			}

			// ???????????£????????????????????????????????????
			inputString = readLine.split(",", 0);

			switch (Integer.parseInt(inputString[2])) {
			case 1:
				smallInk(Integer.parseInt(inputString[0]),
						Integer.parseInt(inputString[1]));
				break;
			case 2:
				mediumInk(Integer.parseInt(inputString[0]),
						Integer.parseInt(inputString[1]));
				break;
			case 3:
				largeInk(Integer.parseInt(inputString[0]),
						Integer.parseInt(inputString[1]));
				break;
			}
		}

		for (int i = 0; i < 10; i++) {
			for (int j = 0; j < 10; j++) {
				if (square[i][j] == 0) {
					whiteTiles++;
				} else {
					if (square[i][j] > mostDeep) {
						mostDeep = square[i][j];
					}
				}
			}
		}
		System.out.println(whiteTiles + "\r\n" + mostDeep);
	}

	/**
	 * ?????????????°??????´????????????
	 *
	 * @param x
	 * @param y
	 */
	static void smallInk(int x, int y) {

		square[x][y - 1]++;
		square[x - 1][y]++;
		square[x][y]++;
		square[x + 1][y]++;
		square[x][y + 1]++;
	}

	/**
	 * ????????????????????´????????????
	 *
	 * @param x
	 * @param y
	 */
	static void mediumInk(int x, int y) {

		square[x - 1][y - 1]++;
		square[x][y - 1]++;
		square[x + 1][y - 1]++;
		square[x - 1][y]++;
		square[x][y]++;
		square[x + 1][y]++;
		square[x - 1][y + 1]++;
		square[x][y + 1]++;
		square[x + 1][y + 1]++;
	}

	/**
	 * ??????????????§?????´????????????
	 *
	 * @param x
	 * @param y
	 */
	static void largeInk(int x, int y) {

		square[x][y - 2]++;
		square[x - 1][y - 1]++;
		square[x][y - 1]++;
		square[x + 1][y - 1]++;
		square[x - 2][y]++;
		square[x - 1][y]++;
		square[x][y]++;
		square[x + 1][y]++;
		square[x + 2][y]++;
		square[x - 1][y + 1]++;
		square[x][y + 1]++;
		square[x + 1][y + 1]++;
		square[x][y + 2]++;
	}
}