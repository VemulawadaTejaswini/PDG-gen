package practice26;

import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;

public class Main {

	// ?´?
	static int paper[][] = new int[10][10];
	// 1???????????????
	static int deepCell = 0;
	// ??????????????????????????????
	static int clearCell = 0;

	public static void main(String[] args) throws IOException {

		InputStreamReader isr = new InputStreamReader(System.in);
		BufferedReader br = new BufferedReader(isr);
		String str;

		while (true) {
			str = br.readLine();
			if (str == null || str.isEmpty()) {
				break;
			}

			// ??\???????????????
			String[] input = str.split(",");

			// ??????????????????????????°??????????????????
			int x = Integer.parseInt(input[0]);
			int y = Integer.parseInt(input[1]);
			int size = Integer.parseInt(input[2]);

			// size?????§??????????????£???????????????????????????????????????
			switch (size) {
			case 1:
				small(x, y);
				break;
			case 2:
				medium(x, y);
				break;
			case 3:
				large(x, y);
				break;
			}
		}
		printClearCell();
		printDeepCell();
	}

	// ?????????????????????????????????????????????
	private static void printClearCell() {
		for (int i = 0; i < 10; ++i) {
			for (int j = 0; j < 10; ++j) {
				if (paper[i][j] == 0) {
					clearCell++;
				}
			}
		}
		System.out.println(clearCell);
	}

	// ?????????????????????????????????
	private static void printDeepCell() {
		for (int i = 0; i < 10; ++i) {
			for (int j = 0; j < 10; ++j) {
				if (deepCell < paper[i][j]) {
					deepCell = paper[i][j];
				}
			}
		}
		System.out.println(deepCell);
	}

	private static void small(int x, int y) {
		// ???????????´??????????????¨???
		paper[x][y]++;
		// ??????????????????????????¨
		if (y != 0) {
			paper[x][y - 1]++;
		}
		// ??????????????????????????¨
		if (y != 9) {
			paper[x][y + 1]++;
		}
		// ??????????????????????????¨
		if (x != 0) {
			paper[x - 1][y]++;
		}
		// ??????????????????????????¨
		if (x != 9) {
			paper[x + 1][y]++;
		}
	}

	private static void medium(int x, int y) {
		small(x, y);
		// ???????????????????????????
		if (x >= 1 && y >= 1) {
			paper[x - 1][y - 1]++;
		}
		// ???????????????????????????
		if (x <= 8 && y >= 1) {
			paper[x + 1][y - 1]++;
		}
		// ???????????????????????????
		if (x >= 1 && y <= 8) {
			paper[x - 1][y + 1]++;
		}
		// ???????????????????????????
		if (x <= 8 && y <= 8) {
			paper[x + 1][y + 1]++;
		}
	}

	private static void large(int x, int y) {
		medium(x, y);
		// ???????????´???2??????
		if (x >= 2) {
			paper[x - 2][y]++;
		}
		// ???????????´???2??????
		if (x <= 7) {
			paper[x + 2][y]++;
		}
		// ???????????´???2??????
		if (y >= 2) {
			paper[x][y - 2]++;
		}
		// ???????????´???2??????
		if (y <= 7) {
			paper[x][y + 2]++;
		}
	}
}