public class Main {

	static int paper[][] = new int[10][10];

	public static void main(String[] args) throws IOException {

		BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
		String str = null;

		while ((str = br.readLine()) != null) {

			// ??\???????????????
			String[] input = str.split(",");

			// ??????????????????????????°??????????????????
			int x = Integer.parseInt(input[0]);
			int y = Integer.parseInt(input[1]);
			int size = Integer.parseInt(input[2]);

			// size?????§??????????????£???????????????????????????????????????

			switch (size) {
			case 1:
				small(x, y);
				break;

			case 2:
				medium(x, y);

			case 3:
				large(x, y);
			}

		}

		int clear = 0;
		int max = 0;

		// ????????????????????????????????¨1???????????????????????????
		for (int i = 0; i < 10; ++i) {
			for (int j = 0; j < 10; ++j) {

				// ???????????????????????????????????°??????
				if (paper[i][j] == 0) {
					clear++;
				}

				// 1???????????????
				if (paper[i][j] > max) {
					max = paper[i][j];
				}
			}
		}

		System.out.println(clear);
		System.out.println(max);

	}

	private static void small(int x, int y) {

		paper[x][y]++;

		// ??????????????????????????¨
		if (y != 0) {
			paper[x][y - 1]++;
		}

		// ??????????????????????????¨
		if (y != 9) {
			paper[x][y + 1]++;
		}

		// ??????????????????????????¨
		if (x != 0) {
			paper[x - 1][y]++;
		}

		// ??????????????????????????¨
		if (x != 9) {
			paper[x + 1][y]++;
		}

	}

	private static void medium(int x, int y) {
		small(x, y);

		// ???????????????????????????
		if (x >= 1 && y >= 1) {
			paper[x - 1][y - 1]++;
		}

		// ???????????????????????????
		if (x <= 8 && y >= 1) {
			paper[x + 1][y - 1]++;
		}

		// ???????????????????????????
		if (x >= 1 && y <= 8) {
			paper[x - 1][y + 1]++;
		}

		// ???????????????????????????
		if (x <= 8 && y <= 8) {
			paper[x + 1][y + 1]++;
		}
	}

	private static void large(int x, int y) {

		small(x, y);
		medium(x, y);

		// ???????????´???2??????
		if (x >= 2) {
			paper[x - 2][y]++;
		}

		// ???????????´???2??????
		if (x <= 7) {
			paper[x + 2][y]++;
		}

		// ???????????´???2??????
		if (y >= 2) {
			paper[x][y - 2]++;
		}

		// ???????????´???2??????
		if (y <= 7) {
			paper[x][y + 2]++;
		}

	}

}