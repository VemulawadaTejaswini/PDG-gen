import java.io.IOException;
import java.util.Scanner;

public class Main {

	public static void main(String[] args) throws IOException {
		Scanner sc = new Scanner(System.in);
		int A = sc.nextInt();
		int B = sc.nextInt();
		int result = 1;
		int tap = A;
		while(B > tap) {
			tap += A - 1;
			result++;
		}
		System.out.println(result);
		sc.close();
	}

}
