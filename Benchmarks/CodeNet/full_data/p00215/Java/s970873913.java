import java.awt.Point;
import java.util.ArrayList;
import java.util.List;
import java.util.Scanner;
import java.util.regex.Matcher;
import java.util.regex.Pattern;

public class Main {

	/** ????????????????´¢??¨???????????? */
	static final Pattern np = Pattern.compile("[GS]|[1-5]");

	/** ??????????????°??????????´¢??¨???????????? */
	static final Pattern sp = Pattern.compile("[S]");

	/** ??´????????°??????????´¢??¨???????????? */
	static final Pattern gp = Pattern.compile("[G]");

	/** ???????????¢?????°???????´¢??¨???????????? */
	static final Pattern pp = Pattern.compile("[1-5]");

	/** ??????????????????_X */
	static int mapSizeX = 0;

	/** ??????????????????_Y */
	static int mapSizeY = 0;

	/** ??????????????° */
	static int maxNode = 0;

	/** ????????? */
	static String[] map = new String[1000 * 1000];

	/** ????????¢??????????????? */
	static List<Point> pachimonList = new ArrayList<Point>();

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		boolean startFlg = false;
		boolean goalFlg = false;
		Scanner scanner = new Scanner(System.in);

		while (scanner.hasNextLine()) {
			String[] mapSizeLine = scanner.nextLine().split(" ");
			pachimonList.removeAll(pachimonList);
			maxNode = 0;
			mapSizeX = Integer.parseInt(mapSizeLine[0]);
			mapSizeY = Integer.parseInt(mapSizeLine[1]);
			//initializeMap(mapSizeX, mapSizeY);
			for (int i = 0; i < mapSizeY; i++) {
				String mapInfoLine = scanner.nextLine();
				for (int j = 0; j < mapSizeX; j++) {
					map[calcIndex(j, i)] = String.valueOf(mapInfoLine.charAt(j));
					Matcher nm = np.matcher(map[calcIndex(j, i)]);
					if (nm.matches()) {
						maxNode++;
						// ??????????????°??????????´¢
						if (!startFlg) {
							Matcher sm = sp.matcher(map[calcIndex(j, i)]);
							if (sm.matches()) {
								pachimonList.add(createPos(0, calcIndex(j, i)));
								startFlg = true;
							}
						}
						// ??´????????°??????????´¢
						if (!goalFlg) {
							Matcher gm = gp.matcher(map[calcIndex(j, i)]);
							if (gm.matches()) {
								pachimonList.add(createPos(6, calcIndex(j, i)));
								goalFlg = true;
							}
						}
						// ???????????¢????????°????¨????
						Matcher pm = pp.matcher(map[calcIndex(j, i)]);
						if (pm.matches()) {
							pachimonList.add(createPos(Integer.valueOf(map[calcIndex(j, i)]), calcIndex(j, i)));
						}
					}
				}
			}
		}
	}

	/**
	 * ??????????????¨??????????´???????????????§?????????
	 */
	private static void initializeMap(int sizeX, int sizeY) {
		for (int i = 0; i < sizeX * sizeY; i++) {
			map[i] = "";
		}
	}

	/**
	 * X????????????????????´?????????????????¢???????±???§????¨????<br>
	 * ????????????:0?????´??????:6???????????¢???????±???§:1???5<br>
	 * Y????????????????????????????????????????´?
	 * @param type ?????????????????´?????????????????¢???????±???§
	 * @param index ??????????????????????????????
	 * @return
	 */
	private static Point createPos(int type, int index) {
		return new Point(type, index);
	}

	/**
	 * X??§?¨???¨Y??§?¨???????????????£?????????????????????????????????????????´?????????
	 *
	 * @param x X??§?¨?
	 * @param y Y??§?¨?
	 * @return
	 */
	private static int calcIndex(int x, int y) {
		return x * mapSizeY + y;
	}
}